// ============================================================================
// W3b fusion helper: allocate + populate F16 copies of the LN gammas.
// aclnnAddRmsNorm requires gamma to match x1/x2 dtype (F16 here); the unfused
// aclnnRmsNorm path kept them F32. We use aclnnCast to down-convert on device
// without bouncing through host memory. No-op unless cp_fusion_applied_.
// ============================================================================
void CpCannEngine::init_fusion_f16_gammas_() {
    if (!cp_fusion_applied_) return;

    const size_t E = sizeof(uint16_t);
    auto cast_gamma = [&](void *src_f32_dev, void **dst_f16_dev_out) {
        alloc_dev(dst_f16_dev_out, (size_t)cp_hidden_ * E);
        int64_t shape[1]   = {(int64_t)cp_hidden_};
        int64_t strides[1] = {1};
        int64_t storage    = (int64_t)cp_hidden_;
        aclTensor *t_src = g_cann.aclCreateTensor(
            shape, 1, ACL_FLOAT, strides, 0, ACL_FORMAT_ND,
            &storage, 1, src_f32_dev);
        aclTensor *t_dst = g_cann.aclCreateTensor(
            shape, 1, ACL_FLOAT16, strides, 0, ACL_FORMAT_ND,
            &storage, 1, *dst_f16_dev_out);
        CANN_OP(Cast, t_src, ACL_FLOAT16, t_dst);
        g_cann.aclDestroyTensor(t_src);
        g_cann.aclDestroyTensor(t_dst);
    };
    for (int il = 0; il < n_layers_; ++il) {
        cast_gamma(layer_w_[il].input_ln_w, &layer_w_[il].input_ln_w_f16);
        cast_gamma(layer_w_[il].post_ln_w,  &layer_w_[il].post_ln_w_f16);
    }
    cast_gamma(final_norm_w_dev_, &final_norm_w_f16_dev_);
    // Cast dispatches were queued on stream_; sync so the F16 buffers are
    // valid before the persistent tensors wrap them.
    ACL_CHECK_RET(g_cann.aclrtSynchronizeStream(stream_));
}

// ============================================================================
// Path C W4.1 AscendC: allocate + populate F16 copies of the per-head Q/K
// RmsNorm gammas the fused attn-sublayer kernel reads directly. When ASCENDC
// is enabled without FUSION (unusual, but valid), also create the
// input_ln / final_norm F16 copies the kernel's RmsNorm branch needs.
// No-op unless cp_ascendc_applied_.
// ============================================================================
void CpCannEngine::init_ascendc_f16_gammas_() {
    if (!cp_ascendc_applied_) return;

    const size_t E = sizeof(uint16_t);
    auto cast_gamma = [&](void *src_f32_dev, void **dst_f16_dev_out,
                           int64_t len) {
        if (*dst_f16_dev_out) return;  // already cast by fusion path
        alloc_dev(dst_f16_dev_out, (size_t)len * E);
        int64_t shape[1]   = {len};
        int64_t strides[1] = {1};
        int64_t storage    = len;
        aclTensor *t_src = g_cann.aclCreateTensor(
            shape, 1, ACL_FLOAT, strides, 0, ACL_FORMAT_ND,
            &storage, 1, src_f32_dev);
        aclTensor *t_dst = g_cann.aclCreateTensor(
            shape, 1, ACL_FLOAT16, strides, 0, ACL_FORMAT_ND,
            &storage, 1, *dst_f16_dev_out);
        CANN_OP(Cast, t_src, ACL_FLOAT16, t_dst);
        g_cann.aclDestroyTensor(t_src);
        g_cann.aclDestroyTensor(t_dst);
    };

    for (int il = 0; il < n_layers_; ++il) {
        cast_gamma(layer_w_[il].q_norm_w,  &layer_w_[il].q_norm_w_f16,
                    head_dim_);
        cast_gamma(layer_w_[il].k_norm_w,  &layer_w_[il].k_norm_w_f16,
                    head_dim_);
        // Safety net: if fusion wasn't on, its F16 gammas don't exist. The
        // kernel's opts=1 branch does read normed_dev_ from GM, but opts=0
        // would still need input_ln_w_f16 — cheap insurance.
        cast_gamma(layer_w_[il].input_ln_w, &layer_w_[il].input_ln_w_f16,
                    cp_hidden_);
        cast_gamma(layer_w_[il].post_ln_w,  &layer_w_[il].post_ln_w_f16,
                    cp_hidden_);
    }
    cast_gamma(final_norm_w_dev_, &final_norm_w_f16_dev_, cp_hidden_);

    ACL_CHECK_RET(g_cann.aclrtSynchronizeStream(stream_));
}

void CpCannEngine::w8_matmul_(const aclTensor *x,
                                void *weight_dev, void *scale_dev,
                                int64_t out_n, int64_t in_k,
                                const aclTensor *y) {
    int64_t w_shape[2]   = {in_k, out_n};
    int64_t w_strides[2] = {1, in_k};
    int64_t w_storage    = out_n * in_k;
    aclTensor *t_w = g_cann.aclCreateTensor(
        w_shape, 2, ACL_INT8, w_strides, 0, ACL_FORMAT_ND,
        &w_storage, 1, weight_dev);

    int64_t s_shape[1]   = {out_n};
    int64_t s_strides[1] = {1};
    int64_t s_storage    = out_n;
    aclTensor *t_scale = g_cann.aclCreateTensor(
        s_shape, 1, ACL_FLOAT16, s_strides, 0, ACL_FORMAT_ND,
        &s_storage, 1, scale_dev);

    uint64_t ws_needed = 0;
    aclOpExecutor *exec = nullptr;
    aclnnStatus s = 0;
    bool used_v3 = false;
    if (g_cann.aclnnWeightQuantBatchMatmulV3GetWorkspaceSize &&
        g_cann.aclnnWeightQuantBatchMatmulV3) {
        s = g_cann.aclnnWeightQuantBatchMatmulV3GetWorkspaceSize(
            x, t_w, t_scale,
            /*antiquantOffset*/ nullptr,
            /*quantScale*/      nullptr,
            /*quantOffset*/     nullptr,
            /*bias*/            nullptr,
            /*antiquantGroupSize*/ 0,
            /*innerPrecise*/       1,
            y, &ws_needed, &exec);
        if (s == 0) used_v3 = true;
    }
    if (!used_v3) {
        if (!g_cann.aclnnWeightQuantBatchMatmulV2GetWorkspaceSize ||
            !g_cann.aclnnWeightQuantBatchMatmulV2) {
            fprintf(stderr, "[cp_cann] w8_matmul: no V3/V2 symbol resolved\n");
            g_cann.aclDestroyTensor(t_w);
            g_cann.aclDestroyTensor(t_scale);
            return;
        }
        s = g_cann.aclnnWeightQuantBatchMatmulV2GetWorkspaceSize(
            x, t_w, t_scale, nullptr, nullptr, nullptr, nullptr,
            /*antiquantGroupSize*/ 0, y, &ws_needed, &exec);
        if (s != 0) {
            fprintf(stderr,
                    "[cp_cann] w8_matmul V2 GetWorkspaceSize status=%d\n",
                    (int)s);
            g_cann.aclDestroyTensor(t_w);
            g_cann.aclDestroyTensor(t_scale);
            return;
        }
    }
    ensure_workspace((size_t)ws_needed);
    void *ws = ws_needed > 0 ? workspace_dev_ : nullptr;
    if (used_v3) {
        s = g_cann.aclnnWeightQuantBatchMatmulV3(ws, ws_needed, exec, stream_);
    } else {
        s = g_cann.aclnnWeightQuantBatchMatmulV2(ws, ws_needed, exec, stream_);
    }
    if (s != 0) {
        fprintf(stderr, "[cp_cann] w8_matmul %s launch status=%d\n",
                used_v3 ? "V3" : "V2", (int)s);
    }
    g_cann.aclDestroyTensor(t_w);
    g_cann.aclDestroyTensor(t_scale);
}

// ============================================================================
// Phase GMM-wire: aclnnGroupedMatmulV3 fused Q/K/V
// ============================================================================
//
// Replaces the three sequential w8_matmul_ calls that produce Q/K/V at the
// attention-sublayer entry with a single aclnnGroupedMatmulV3 dispatch. All
// three "groups" share the same activation — we pass three distinct
// aclTensor views wrapping the same device buffer (aclDestroyTensorList
// consumes the contained tensors, so fresh views must be built every call).
//
// The shared zero-F16 antiquantOffset buffer is allocated once at init
// (size = q_dim_ = max(Nq, Nk, Nv)); each group wraps a prefix view whose
// length matches that group's N.
//
// Preconditions enforced at the call site:
//   * cp_gmm_qkv_applied_
//   * antiquant_zero_offset_dev_ != nullptr
//   * layer_w_[il].{q,k,v}_proj_{w_i8,scale} populated (w8_applied_)
//
// Argument semantics mirror the probe at /tmp/qkv_grouped_probe/
// test_qkv_grouped.cpp: x_dev is the activation (F16 [1, cp_hidden]);
// q_out_dev / k_out_dev / v_out_dev are the destination buffers sized
// q_dim_, kv_dim_, kv_dim_ respectively.
void CpCannEngine::gmm_qkv_(void *x_dev, int64_t il,
                             void *q_out_dev, void *k_out_dev,
                             void *v_out_dev) {
    // Fresh tensor views. Per the probe: aclDestroyTensorList consumes the
    // contained tensors and the op pipeline cannot reuse stale handles, so
    // every dispatch rebuilds its own.
    auto mk_x = [&]() {
        int64_t shape[2]   = {1, (int64_t)cp_hidden_};
        int64_t strides[2] = {(int64_t)cp_hidden_, 1};
        int64_t storage    = cp_hidden_;
        return g_cann.aclCreateTensor(shape, 2, ACL_FLOAT16, strides, 0,
                                       ACL_FORMAT_ND, &storage, 1, x_dev);
    };
    auto mk_w = [&](void *w_dev, int64_t N) {
        // Mirror w8_matmul_'s weight layout: logical [K,N] over a physical
        // [N,K] INT8 blob via strides {1, K}.
        int64_t shape[2]   = {(int64_t)cp_hidden_, N};
        int64_t strides[2] = {1, (int64_t)cp_hidden_};
        int64_t storage    = N * (int64_t)cp_hidden_;
        return g_cann.aclCreateTensor(shape, 2, ACL_INT8, strides, 0,
                                       ACL_FORMAT_ND, &storage, 1, w_dev);
    };
    auto mk_1d_f16 = [&](void *d, int64_t N) {
        int64_t shape[1]   = {N};
        int64_t strides[1] = {1};
        int64_t storage    = N;
        return g_cann.aclCreateTensor(shape, 1, ACL_FLOAT16, strides, 0,
                                       ACL_FORMAT_ND, &storage, 1, d);
    };
    auto mk_y = [&](void *buf, int64_t N) {
        int64_t shape[2]   = {1, N};
        int64_t strides[2] = {N, 1};
        int64_t storage    = N;
        return g_cann.aclCreateTensor(shape, 2, ACL_FLOAT16, strides, 0,
                                       ACL_FORMAT_ND, &storage, 1, buf);
    };

    std::vector<const aclTensor *> xs  = {mk_x(), mk_x(), mk_x()};
    std::vector<const aclTensor *> ws_ = {mk_w(layer_w_[il].q_proj_w_i8, q_dim_),
                                          mk_w(layer_w_[il].k_proj_w_i8, kv_dim_),
                                          mk_w(layer_w_[il].v_proj_w_i8, kv_dim_)};
    std::vector<const aclTensor *> asc = {mk_1d_f16(layer_w_[il].q_proj_scale, q_dim_),
                                          mk_1d_f16(layer_w_[il].k_proj_scale, kv_dim_),
                                          mk_1d_f16(layer_w_[il].v_proj_scale, kv_dim_)};
    // Shared zero-F16 antiquantOffset buffer. Each group wraps a prefix view
    // sized to that group's N. Identical dev pointers across groups are
    // accepted by the runtime so long as the aclTensor handles are distinct.
    std::vector<const aclTensor *> aoc = {mk_1d_f16(antiquant_zero_offset_dev_, q_dim_),
                                          mk_1d_f16(antiquant_zero_offset_dev_, kv_dim_),
                                          mk_1d_f16(antiquant_zero_offset_dev_, kv_dim_)};
    std::vector<const aclTensor *> ys  = {mk_y(q_out_dev, q_dim_),
                                          mk_y(k_out_dev, kv_dim_),
                                          mk_y(v_out_dev, kv_dim_)};

    aclTensorList *x_list  = g_cann.aclCreateTensorList(xs.data(),  xs.size());
    aclTensorList *w_list  = g_cann.aclCreateTensorList(ws_.data(), ws_.size());
    aclTensorList *as_list = g_cann.aclCreateTensorList(asc.data(), asc.size());
    aclTensorList *ao_list = g_cann.aclCreateTensorList(aoc.data(), aoc.size());
    aclTensorList *y_list  = g_cann.aclCreateTensorList(ys.data(),  ys.size());

    uint64_t ws_needed = 0;
    aclOpExecutor *exec = nullptr;
    aclnnStatus s = g_cann.aclnnGroupedMatmulV3GetWorkspaceSize(
        x_list, w_list,
        /*biasOptional*/             nullptr,
        /*scaleOptional*/            nullptr,   // post-quant scale (W8A8 lane)
        /*offsetOptional*/           nullptr,
        /*antiquantScaleOptional*/   as_list,
        /*antiquantOffsetOptional*/  ao_list,
        /*groupListOptional*/        nullptr,
        /*splitItem*/                0,
        /*groupType*/                -1,
        y_list, &ws_needed, &exec);
    if (s != 0) {
        fprintf(stderr,
                "[cp_cann] gmm_qkv V3 GetWorkspaceSize status=%d (il=%ld)\n",
                (int)s, (long)il);
        g_cann.aclDestroyTensorList(x_list);
        g_cann.aclDestroyTensorList(w_list);
        g_cann.aclDestroyTensorList(as_list);
        g_cann.aclDestroyTensorList(ao_list);
        g_cann.aclDestroyTensorList(y_list);
        return;
    }

    ensure_workspace((size_t)ws_needed);
    void *ws = ws_needed > 0 ? workspace_dev_ : nullptr;
    s = g_cann.aclnnGroupedMatmulV3(ws, ws_needed, exec, stream_);
    if (s != 0) {
        fprintf(stderr, "[cp_cann] gmm_qkv V3 launch status=%d (il=%ld)\n",
                (int)s, (long)il);
    }

    g_cann.aclDestroyTensorList(x_list);
    g_cann.aclDestroyTensorList(w_list);
    g_cann.aclDestroyTensorList(as_list);
    g_cann.aclDestroyTensorList(ao_list);
    g_cann.aclDestroyTensorList(y_list);
}

// ============================================================================
// Phase B: aclnnFFNV3 (fused SwiGLU FFN) — weight pack + dispatch
// ============================================================================

// Build per-layer gate||up concatenated INT8 weight + F16 scale blobs. The
// two source buffers (gate_proj_w_i8, up_proj_w_i8) are row-major
// [inter, cp_hidden] INT8, their scales are [inter] F16 (per-output-channel).
// FFNV3 wants weight1 = [K1, N1] = [cp_hidden, 2*inter] logical; we keep the
// physical storage as row-major [2*inter, cp_hidden] (byte-concat of the two
// blobs) and view it with strides [1, cp_hidden] at dispatch time — the same
// trick w8_matmul_ uses for single matmuls. Scales concat [inter]+[inter] →
// [2*inter].
//
// SwiGLU convention: CANN's aclnnFFNV3 with activation="swiglu" appears to
// treat the concatenation as [up || gate] with N1 split into halves where
// first-half is the "up" (linear) branch and second-half is the "gate"
// (silu-activated) branch. We pack as [gate || up] initially; if parity
// fails, swap to [up || gate] (see TALKER_CP_FFN_V3_ORDER env).
//
// Default convention (gate-first): matches HF LLaMA/Qwen naming.
// Alternate (up-first): matches some fused kernels — enable with
// TALKER_CP_FFN_V3_ORDER=up_first.
bool CpCannEngine::build_ffn_v3_weights_() {
    if (!w8_applied_) return false;
    if (layer_w_.empty()) return false;

    // Determine concat order. Default is gate-first (matches HF naming).
    // Override with TALKER_CP_FFN_V3_ORDER=up_first to swap branches.
    bool up_first = false;
    {
        const char *env = getenv("TALKER_CP_FFN_V3_ORDER");
        if (env && std::strcmp(env, "up_first") == 0) {
            up_first = true;
            printf("[cp_cann] Phase B: FFNV3 weight order = up_first "
                   "(TALKER_CP_FFN_V3_ORDER=%s)\n", env);
        } else {
            printf("[cp_cann] Phase B: FFNV3 weight order = gate_first "
                   "(default; set TALKER_CP_FFN_V3_ORDER=up_first to swap)\n");
        }
    }

    const size_t per_proj_bytes   = (size_t)inter_ * cp_hidden_;         // INT8
    const size_t per_scale_bytes  = (size_t)inter_ * sizeof(uint16_t);   // F16
    const size_t concat_w_bytes   = per_proj_bytes * 2;
    const size_t concat_s_bytes   = per_scale_bytes * 2;

    for (int il = 0; il < (int)layer_w_.size(); ++il) {
        auto &lw = layer_w_[il];
        if (!lw.gate_proj_w_i8 || !lw.up_proj_w_i8 ||
            !lw.gate_proj_scale || !lw.up_proj_scale) {
            fprintf(stderr, "[cp_cann] build_ffn_v3_weights_: layer %d W8 "
                            "buffers missing (gate/up not calibrated)\n", il);
            return false;
        }

        void *first_w  = up_first ? lw.up_proj_w_i8  : lw.gate_proj_w_i8;
        void *second_w = up_first ? lw.gate_proj_w_i8 : lw.up_proj_w_i8;
        void *first_s  = up_first ? lw.up_proj_scale  : lw.gate_proj_scale;
        void *second_s = up_first ? lw.gate_proj_scale : lw.up_proj_scale;

        // Weight concat: [first | second] D2D → gate_up_w_i8.
        void *w_dev = nullptr;
        aclError err = g_cann.aclrtMalloc(&w_dev, concat_w_bytes,
                                           ACL_MEM_MALLOC_HUGE_FIRST);
        if (err != 0 || !w_dev) {
            fprintf(stderr, "[cp_cann] build_ffn_v3_weights_: aclrtMalloc "
                            "weight l=%d err=%d\n", il, (int)err);
            return false;
        }
        ACL_CHECK_RET(g_cann.aclrtMemcpy(
            w_dev, per_proj_bytes,
            first_w, per_proj_bytes,
            ACL_MEMCPY_DEVICE_TO_DEVICE));
        ACL_CHECK_RET(g_cann.aclrtMemcpy(
            (uint8_t *)w_dev + per_proj_bytes, per_proj_bytes,
            second_w, per_proj_bytes,
            ACL_MEMCPY_DEVICE_TO_DEVICE));

        // Scale concat: [first | second] D2D → gate_up_scale.
        void *s_dev = nullptr;
        err = g_cann.aclrtMalloc(&s_dev, concat_s_bytes,
                                  ACL_MEM_MALLOC_HUGE_FIRST);
        if (err != 0 || !s_dev) {
            fprintf(stderr, "[cp_cann] build_ffn_v3_weights_: aclrtMalloc "
                            "scale l=%d err=%d\n", il, (int)err);
            g_cann.aclrtFree(w_dev);
            return false;
        }
        ACL_CHECK_RET(g_cann.aclrtMemcpy(
            s_dev, per_scale_bytes,
            first_s, per_scale_bytes,
            ACL_MEMCPY_DEVICE_TO_DEVICE));
        ACL_CHECK_RET(g_cann.aclrtMemcpy(
            (uint8_t *)s_dev + per_scale_bytes, per_scale_bytes,
            second_s, per_scale_bytes,
            ACL_MEMCPY_DEVICE_TO_DEVICE));

        lw.gate_up_w_i8  = w_dev;
        lw.gate_up_scale = s_dev;
    }

    ACL_CHECK_RET(g_cann.aclrtSynchronizeStream(stream_));
    return true;
}

// Dispatch aclnnFFNV3 for one row. Caller provides the input + output F16
// [1, cp_hidden] aclTensors plus the four device buffers (gate||up INT8 +
// scale, down INT8 + scale). We build the weight/scale aclTensors fresh
// each call — they're cheap aclTensor handles over persistent device mem.
void CpCannEngine::ffn_v3_swiglu_(const aclTensor *x_row,
                                    void *gate_up_i8, void *gate_up_s,
                                    void *down_i8,    void *down_s,
                                    const aclTensor *y_row) {
    const int64_t two_inter = 2 * (int64_t)inter_;

    // weight1: INT8 view as logical [K1, N1] = [cp_hidden, 2*inter]. The
    // underlying storage is row-major [2*inter, cp_hidden] (byte-concat
    // of the two per-out-channel INT8 blobs). Strides [1, cp_hidden] present
    // the transposed view FFNV3 consumes. Same trick as w8_matmul_.
    int64_t w1_shape[2]   = {cp_hidden_, two_inter};
    int64_t w1_strides[2] = {1, cp_hidden_};
    int64_t w1_storage    = two_inter * cp_hidden_;
    aclTensor *t_w1 = g_cann.aclCreateTensor(
        w1_shape, 2, ACL_INT8, w1_strides, 0, ACL_FORMAT_ND,
        &w1_storage, 1, gate_up_i8);

    // weight2: INT8 [K2, N2] = [inter, cp_hidden] (down_proj). Storage is
    // row-major [cp_hidden, inter]; strides [1, inter] view as [inter, cp_hidden].
    int64_t w2_shape[2]   = {inter_, cp_hidden_};
    int64_t w2_strides[2] = {1, inter_};
    int64_t w2_storage    = cp_hidden_ * inter_;
    aclTensor *t_w2 = g_cann.aclCreateTensor(
        w2_shape, 2, ACL_INT8, w2_strides, 0, ACL_FORMAT_ND,
        &w2_storage, 1, down_i8);

    // antiquantScale1: F16 [2*inter].
    int64_t s1_shape[1]   = {two_inter};
    int64_t s1_strides[1] = {1};
    int64_t s1_storage    = two_inter;
    aclTensor *t_aqs1 = g_cann.aclCreateTensor(
        s1_shape, 1, ACL_FLOAT16, s1_strides, 0, ACL_FORMAT_ND,
        &s1_storage, 1, gate_up_s);

    // antiquantScale2: F16 [cp_hidden].
    int64_t s2_shape[1]   = {cp_hidden_};
    int64_t s2_strides[1] = {1};
    int64_t s2_storage    = cp_hidden_;
    aclTensor *t_aqs2 = g_cann.aclCreateTensor(
        s2_shape, 1, ACL_FLOAT16, s2_strides, 0, ACL_FORMAT_ND,
        &s2_storage, 1, down_s);

    uint64_t ws_needed = 0;
    aclOpExecutor *exec = nullptr;
    // innerPrecise=1 = high-performance (matches w8_matmul_'s choice; per the
    // header, this flag only affects FLOAT16 paths — our x is F16 so it
    // applies).
    aclnnStatus s = g_cann.aclnnFFNV3GetWorkspaceSize(
        /*x*/               x_row,
        /*weight1*/         t_w1,
        /*weight2*/         t_w2,
        /*expertTokens*/    nullptr,
        /*bias1*/           nullptr,
        /*bias2*/           nullptr,
        /*scale*/           nullptr,
        /*offset*/          nullptr,
        /*deqScale1*/       nullptr,
        /*deqScale2*/       nullptr,
        /*antiquantScale1*/ t_aqs1,
        /*antiquantScale2*/ t_aqs2,
        /*antiquantOffset1*/ nullptr,
        /*antiquantOffset2*/ nullptr,
        /*activation*/      "swiglu",
        /*innerPrecise*/    1,
        /*tokensIndexFlag*/ false,
        /*y*/               y_row,
        &ws_needed, &exec);
    if (s != 0) {
        fprintf(stderr, "[cp_cann] aclnnFFNV3GetWorkspaceSize status=%d msg=%s\n",
                (int)s,
                g_cann.aclGetRecentErrMsg ? g_cann.aclGetRecentErrMsg() : "?");
        g_cann.aclDestroyTensor(t_w1);
        g_cann.aclDestroyTensor(t_w2);
        g_cann.aclDestroyTensor(t_aqs1);
        g_cann.aclDestroyTensor(t_aqs2);
        return;
    }
    ensure_workspace((size_t)ws_needed);
    void *ws = ws_needed > 0 ? workspace_dev_ : nullptr;
    s = g_cann.aclnnFFNV3(ws, ws_needed, exec, stream_);
    if (s != 0) {
        fprintf(stderr, "[cp_cann] aclnnFFNV3 launch status=%d msg=%s\n",
                (int)s,
                g_cann.aclGetRecentErrMsg ? g_cann.aclGetRecentErrMsg() : "?");
    }

    g_cann.aclDestroyTensor(t_w1);
    g_cann.aclDestroyTensor(t_w2);
    g_cann.aclDestroyTensor(t_aqs1);
    g_cann.aclDestroyTensor(t_aqs2);
}

// ============================================================================
// Persistent aclTensor construction / destruction
// ============================================================================

void CpCannEngine::build_persistent_tensors_() {
    const int group = n_heads_ / n_kv_;

    // Input-projection tensors — these stay F32. Transformer weights below
    // remain F16. The F16 view of proj_w/proj_b is unused (we upload F32 now),
    // but the member still exists in the struct — just point it at the F32
    // buffer with F32 dtype for symmetry.
    t_.proj_w = tensor_2d(proj_w_dev_, cp_hidden_, talker_hidden_, ACL_FLOAT);
    t_.proj_b = tensor_1d(proj_b_dev_, cp_hidden_,                 ACL_FLOAT);
    t_.proj_w_f32 = t_.proj_w;
    t_.proj_b_f32 = t_.proj_b;
    t_.proj_out_f32_col  = tensor_2d(proj_out_f32_dev_, cp_hidden_, 1,
                                      ACL_FLOAT);
    t_.proj_out_f32_flat = tensor_1d(proj_out_f32_dev_, cp_hidden_,
                                      ACL_FLOAT);
    t_.cur_f16_flat_as_target = tensor_1d(cur_dev_, cp_hidden_,
                                           ACL_FLOAT16);
    t_.accum_scratch_f32_flat = tensor_1d(accum_scratch_f32_dev_,
                                           cp_hidden_, ACL_FLOAT);
    t_.accum_f32_row_2d   = tensor_2d(proj_out_f32_dev_, 1, cp_hidden_,
                                       ACL_FLOAT);
    t_.normed_f32_row_2d  = tensor_2d(normed_f32_dev_, 1, cp_hidden_,
                                       ACL_FLOAT);
    t_.normed_f32_flat    = tensor_1d(normed_f32_dev_, cp_hidden_, ACL_FLOAT);

    // M5.3 — for the ND fallback path keep the weight descriptors as plain
    // [out, in] ND tensors (self=weight, mat2=activation ordering of
    // aclnnMm). For the NZ path, forward_one_token builds transposed
    // [in, out] NZ-tagged weight descriptors inline, because aclnnMm with
    // FRACTAL_NZ dispatches the NZ kernel only when the weight is mat2
    // (RHS) — the same convention ggml-cann uses in ggml-cann.cpp.
    // Activation becomes self (LHS). The persistent [out, in] ND
    // descriptors created below are only used by the fallback path.
    if (nz_applied_) {
        printf("[cp_cann] M5.3 NZ matmul enabled — forward_one_token will "
               "dispatch aclnnMm with transposed FRACTAL_NZ-tagged weight "
               "descriptors (ggml-cann pattern)\n");
    }
    t_.layer.resize(n_layers_);
    for (int il = 0; il < n_layers_; ++il) {
        auto &lw = layer_w_[il];
        auto &lt = t_.layer[il];
        lt.q_proj    = tensor_2d(lw.q_proj_w,    q_dim_,     cp_hidden_, ACL_FLOAT16);
        lt.k_proj    = tensor_2d(lw.k_proj_w,    kv_dim_,    cp_hidden_, ACL_FLOAT16);
        lt.v_proj    = tensor_2d(lw.v_proj_w,    kv_dim_,    cp_hidden_, ACL_FLOAT16);
        lt.o_proj    = tensor_2d(lw.o_proj_w,    cp_hidden_, q_dim_,     ACL_FLOAT16);
        // Norms kept F32 to match GGUF storage + aclnnRmsNorm SupportInfo[0].
        lt.q_norm    = tensor_1d(lw.q_norm_w,    head_dim_,  ACL_FLOAT);
        lt.k_norm    = tensor_1d(lw.k_norm_w,    head_dim_,  ACL_FLOAT);
        lt.gate_proj = tensor_2d(lw.gate_proj_w, inter_,     cp_hidden_, ACL_FLOAT16);
        lt.up_proj   = tensor_2d(lw.up_proj_w,   inter_,     cp_hidden_, ACL_FLOAT16);
        lt.down_proj = tensor_2d(lw.down_proj_w, cp_hidden_, inter_,     ACL_FLOAT16);
        lt.input_ln  = tensor_1d(lw.input_ln_w,  cp_hidden_, ACL_FLOAT);
        lt.post_ln   = tensor_1d(lw.post_ln_w,   cp_hidden_, ACL_FLOAT);
        // W3b: F16 gamma tensors for aclnnAddRmsNorm. Only when fusion is
        // applied — otherwise `*_f16` buffers are null and the tensor handles
        // stay null (unused by the unfused path).
        if (cp_fusion_applied_) {
            lt.input_ln_f16 = tensor_1d(lw.input_ln_w_f16, cp_hidden_,
                                          ACL_FLOAT16);
            lt.post_ln_f16  = tensor_1d(lw.post_ln_w_f16,  cp_hidden_,
                                          ACL_FLOAT16);
        }
    }
    t_.final_norm = tensor_1d(final_norm_w_dev_, cp_hidden_, ACL_FLOAT);
    if (cp_fusion_applied_) {
        t_.final_norm_f16 = tensor_1d(final_norm_w_f16_dev_, cp_hidden_,
                                        ACL_FLOAT16);
    }

    t_.cur_row       = tensor_2d(cur_dev_, 1, cp_hidden_);
    t_.cur_flat      = tensor_1d(cur_dev_, cp_hidden_);
    t_.residual_flat = tensor_1d(residual_dev_, cp_hidden_);
    t_.normed_row    = tensor_2d(normed_dev_, 1,         cp_hidden_);
    t_.normed_col    = tensor_2d(normed_dev_, cp_hidden_, 1);

    t_.q_col    = tensor_2d(q_dev_, q_dim_, 1);
    t_.q_heads  = tensor_2d(q_dev_, n_heads_, head_dim_);
    {
        int64_t shape[4]   = {1, 1, (int64_t)n_heads_, (int64_t)head_dim_};
        int64_t strides[4] = {(int64_t)q_dim_, (int64_t)q_dim_,
                               (int64_t)head_dim_, 1};
        t_.q_rope_4d = tensor_strided(q_dev_, 4, shape, strides);
    }
    {
        int64_t shape[3]   = {(int64_t)n_kv_, (int64_t)group,
                               (int64_t)head_dim_};
        int64_t strides[3] = {(int64_t)group * head_dim_,
                               (int64_t)head_dim_, 1};
        t_.q_gqa = tensor_strided(q_dev_, 3, shape, strides);
    }

    t_.k_col = tensor_2d(k_dev_, kv_dim_, 1);
    t_.k_kv  = tensor_2d(k_dev_, n_kv_, head_dim_);

    t_.v_col = tensor_2d(v_dev_, kv_dim_, 1);

    t_.attn_out_col = tensor_2d(attn_out_dev_, q_dim_, 1);
    {
        int64_t shape[4]   = {1, 1, (int64_t)n_heads_, (int64_t)head_dim_};
        int64_t strides[4] = {(int64_t)q_dim_, (int64_t)q_dim_,
                               (int64_t)head_dim_, 1};
        t_.attn_out_4d = tensor_strided(attn_out_dev_, 4, shape, strides);
    }
    {
        int64_t shape[3]   = {(int64_t)n_kv_, (int64_t)group,
                               (int64_t)head_dim_};
        int64_t strides[3] = {(int64_t)group * head_dim_,
                               (int64_t)head_dim_, 1};
        t_.attn_out_gqa = tensor_strided(attn_out_dev_, 3, shape, strides);
    }

    t_.o_out_col  = tensor_2d(o_out_dev_, cp_hidden_, 1);
    t_.o_out_flat = tensor_1d(o_out_dev_, cp_hidden_);

    t_.gate_col  = tensor_2d(gate_dev_, inter_, 1);
    t_.gate_flat = tensor_1d(gate_dev_, inter_);
    t_.up_col    = tensor_2d(up_dev_,   inter_, 1);
    t_.up_flat   = tensor_1d(up_dev_,   inter_);

    t_.ffn_out_col  = tensor_2d(ffn_out_dev_, cp_hidden_, 1);
    t_.ffn_out_flat = tensor_1d(ffn_out_dev_, cp_hidden_);

    // rstd MUST be F32 even with F16 input — aclnnRmsNorm only supports
    // rstd dtype=F32 on Ascend (validated against the op's SupportInfo list).
    t_.rstd_11    = tensor_2d(rstd_dev_, 1,        1,    ACL_FLOAT);
    t_.rstd_heads = tensor_2d(rstd_dev_, n_heads_, 1,    ACL_FLOAT);
    t_.rstd_kv    = tensor_2d(rstd_dev_, n_kv_,    1,    ACL_FLOAT);

    // --- Boundary staging tensors (F32) for aclnnCast entry/exit --
    t_.input_f32  = tensor_1d(input_stage_f32_dev_,  talker_hidden_, ACL_FLOAT);
    t_.input_f16  = tensor_1d(q_dev_,                talker_hidden_, ACL_FLOAT16);
    t_.output_f16 = tensor_1d(normed_dev_,           cp_hidden_,     ACL_FLOAT16);
    t_.output_f32 = tensor_1d(output_stage_f32_dev_, cp_hidden_,     ACL_FLOAT);
}

void CpCannEngine::destroy_persistent_tensors_() {
    auto drop = [](aclTensor *&t) {
        if (t) { g_cann.aclDestroyTensor(t); t = nullptr; }
    };
    drop(t_.proj_w);
    drop(t_.proj_b);
    for (auto &lt : t_.layer) {
        drop(lt.q_proj); drop(lt.k_proj); drop(lt.v_proj); drop(lt.o_proj);
        drop(lt.q_norm); drop(lt.k_norm);
        drop(lt.gate_proj); drop(lt.up_proj); drop(lt.down_proj);
        drop(lt.input_ln); drop(lt.post_ln);
        drop(lt.input_ln_f16); drop(lt.post_ln_f16);
    }
    t_.layer.clear();
    drop(t_.final_norm);
    drop(t_.final_norm_f16);
    drop(t_.cur_row);   drop(t_.cur_flat);
    drop(t_.residual_flat);
    drop(t_.normed_row); drop(t_.normed_col);
    drop(t_.q_col); drop(t_.q_heads); drop(t_.q_rope_4d); drop(t_.q_gqa);
    drop(t_.k_col); drop(t_.k_kv);
    drop(t_.v_col);
    drop(t_.attn_out_col); drop(t_.attn_out_4d); drop(t_.attn_out_gqa);
    drop(t_.o_out_col); drop(t_.o_out_flat);
    drop(t_.gate_col); drop(t_.gate_flat);
    drop(t_.up_col);   drop(t_.up_flat);
    drop(t_.ffn_out_col); drop(t_.ffn_out_flat);
    drop(t_.rstd_11); drop(t_.rstd_heads); drop(t_.rstd_kv);
    drop(t_.input_f32); drop(t_.input_f16);
    drop(t_.output_f16); drop(t_.output_f32);
    // proj_w_f32 / proj_b_f32 are aliases of proj_w / proj_b (already dropped).
    t_.proj_w_f32 = nullptr;
    t_.proj_b_f32 = nullptr;
    drop(t_.proj_out_f32_col); drop(t_.proj_out_f32_flat);
    drop(t_.cur_f16_flat_as_target);
    drop(t_.accum_scratch_f32_flat);
    drop(t_.accum_f32_row_2d);
    drop(t_.normed_f32_row_2d);
    drop(t_.normed_f32_flat);
}

// ============================================================================
// Minimal safetensors reader for the MLX-extracted CP weight file.
//
// Format: uint64 header_size little-endian | JSON header | raw tensor bytes.
// Header JSON maps `name` → {dtype, shape, data_offsets: [start, end]}.
// We don't pull in a real JSON library — the entries we need are looked up
// by string search, and data_offsets is a flat numeric array.
// ============================================================================

namespace {

struct StEntry {
    uint64_t data_start = 0;  // offset relative to tensor data region
    uint64_t data_end   = 0;
};

// Find `"<name>":{...}` and extract its `"data_offsets":[start,end]`.
bool st_lookup(const std::string &header, const std::string &name,
               StEntry &out) {
    std::string key = "\"" + name + "\"";
    size_t p = header.find(key);
    if (p == std::string::npos) return false;
    size_t do_pos = header.find("\"data_offsets\"", p);
    if (do_pos == std::string::npos) return false;
    size_t lb = header.find('[', do_pos);
    size_t rb = header.find(']', lb);
    if (lb == std::string::npos || rb == std::string::npos) return false;
    std::string inner = header.substr(lb + 1, rb - lb - 1);
    size_t comma = inner.find(',');
    if (comma == std::string::npos) return false;
    out.data_start = std::stoull(inner.substr(0, comma));
    out.data_end   = std::stoull(inner.substr(comma + 1));
    return true;
}

// Convert a buffer of BF16 values (stored as uint16_t big-endian-ish halfs of
// the original F32) into F16 values ready for NPU upload. For values within
// F16's exponent range (true for trained transformer weights), this conversion
// is effectively lossless — F16 has more mantissa bits than BF16, so the
// BF16 → F32 → F16 round-trip fills the extra bits with zeros.
void bf16_buf_to_fp16_buf(const uint16_t *bf16_src,
                           uint16_t *fp16_dst, size_t n) {
    for (size_t i = 0; i < n; ++i) {
        union { uint32_t u; float f; } v;
        v.u = ((uint32_t)bf16_src[i]) << 16;
        __fp16 h = (__fp16)v.f;
        std::memcpy(&fp16_dst[i], &h, sizeof(uint16_t));
    }
}

}  // namespace

bool CpCannEngine::init_from_safetensors(const std::string &path,
                                          const CodePredictorConfig &cfg,
                                          int device) {
    if (!cp_cann_load_symbols()) {
        fprintf(stderr, "[cp_cann] symbol load failed; engine disabled\n");
        return false;
    }

    // Cache dims first.
    device_ = device;
    talker_hidden_ = cfg.talker_hidden_size;
    cp_hidden_     = cfg.hidden_size;
    n_heads_       = cfg.num_attention_heads;
    n_kv_          = cfg.num_key_value_heads;
    head_dim_      = cfg.head_dim;
    q_dim_         = n_heads_ * head_dim_;
    kv_dim_        = n_kv_ * head_dim_;
    inter_         = cfg.intermediate_size;
    n_layers_      = cfg.num_hidden_layers;
    eps_           = cfg.rms_norm_eps;
    rope_theta_    = cfg.rope_theta;

    // Open + read header.
    std::ifstream f(path, std::ios::binary);
    if (!f) {
        fprintf(stderr, "[cp_cann] safetensors open failed: %s\n", path.c_str());
        return false;
    }
    uint64_t header_size = 0;
    f.read(reinterpret_cast<char *>(&header_size), 8);
    std::string header(header_size, '\0');
    f.read(&header[0], header_size);
    const std::streamoff data_base = 8 + (std::streamoff)header_size;

    ACL_CHECK_RET(g_cann.aclrtSetDevice(device_));
    // Primary + secondary streams (M6.1). Primary is the default target;
    // stream_b_ is spare for multi-stream pipelining.
    ACL_CHECK_RET(g_cann.aclrtCreateStream(&primary_stream_));
    stream_ = primary_stream_;
    ACL_CHECK_RET(g_cann.aclrtCreateStream(&stream_b_));
    // M6.2: reusable event recorded at the end of forward_one_token_launch.
    if (g_cann.aclrtCreateEvent && !forward_done_event_) {
        ACL_CHECK_RET(g_cann.aclrtCreateEvent(&forward_done_event_));
    }

    // Helper: load one tensor by name, convert BF16 → F16 on host, upload.
    auto load_one_f16 = [&](const std::string &name, void *&dev,
                            size_t expected_elems) {
        StEntry e;
        if (!st_lookup(header, name, e)) {
            fprintf(stderr, "[cp_cann] missing tensor: %s\n", name.c_str());
            return false;
        }
        size_t nbytes = e.data_end - e.data_start;
        if (nbytes != expected_elems * 2) {
            fprintf(stderr,
                    "[cp_cann] size mismatch for %s: got %zu bytes, "
                    "expected %zu (%zu bf16 elems)\n",
                    name.c_str(), nbytes, expected_elems * 2, expected_elems);
            return false;
        }
        std::vector<uint16_t> bf16_buf(expected_elems);
        f.seekg(data_base + (std::streamoff)e.data_start);
        f.read(reinterpret_cast<char *>(bf16_buf.data()), nbytes);
        std::vector<uint16_t> fp16_buf(expected_elems);
        bf16_buf_to_fp16_buf(bf16_buf.data(), fp16_buf.data(), expected_elems);
        alloc_dev(&dev, expected_elems * 2);
        ACL_CHECK_RET(g_cann.aclrtMemcpy(
            dev, nbytes, fp16_buf.data(), nbytes,
            ACL_MEMCPY_HOST_TO_DEVICE));
        return true;
    };
    // For tensors we keep as F32 on device (norm gammas, proj_w/b for the
    // F32 input projection). BF16 → F32 is exact (just high-bit shift).
    auto load_one_f32 = [&](const std::string &name, void *&dev,
                            size_t expected_elems) {
        StEntry e;
        if (!st_lookup(header, name, e)) {
            fprintf(stderr, "[cp_cann] missing tensor: %s\n", name.c_str());
            return false;
        }
        size_t nbytes = e.data_end - e.data_start;
        if (nbytes != expected_elems * 2) {
            fprintf(stderr, "[cp_cann] size mismatch for %s\n", name.c_str());
            return false;
        }
        std::vector<uint16_t> bf16_buf(expected_elems);
        f.seekg(data_base + (std::streamoff)e.data_start);
        f.read(reinterpret_cast<char *>(bf16_buf.data()), nbytes);
        std::vector<float> f32_buf(expected_elems);
        for (size_t i = 0; i < expected_elems; ++i) {
            union { uint32_t u; float f; } v;
            v.u = ((uint32_t)bf16_buf[i]) << 16;
            f32_buf[i] = v.f;
        }
        alloc_dev(&dev, expected_elems * sizeof(float));
        ACL_CHECK_RET(g_cann.aclrtMemcpy(
            dev, expected_elems * sizeof(float), f32_buf.data(),
            expected_elems * sizeof(float), ACL_MEMCPY_HOST_TO_DEVICE));
        return true;
    };

    const std::string pfx = "talker.code_predictor.";
    const std::string mpfx = pfx + "model.";

    // Input projection: F32 on device to match MLX's F32 input embedding
    // precision + our F32 projection compute path.
    if (!load_one_f32(pfx + "small_to_mtp_projection.weight", proj_w_dev_,
                      (size_t)cp_hidden_ * talker_hidden_)) return false;
    if (!load_one_f32(pfx + "small_to_mtp_projection.bias",   proj_b_dev_,
                      cp_hidden_)) return false;

    // ---- FRACTAL_NZ / A16W8 gating (M5.2 / Stretch S1) ----------------------
    // Env override TALKER_CP_W8_QUANT / TALKER_W8_QUANT — same semantics as
    // the Talker engine. W8 wins over NZ if both are set.
    {
        const char *w8_env = getenv("TALKER_W8_QUANT");
        if (!use_w8_weights_ && w8_env && w8_env[0] != '\0' && w8_env[0] != '0') {
            use_w8_weights_ = true;
            printf("[cp_cann] TALKER_W8_QUANT=%s forcing A16W8 weight path\n",
                   w8_env);
        }
    }
    if (use_w8_weights_ && use_nz_weights_) {
        printf("[cp_cann] W8 and NZ are mutually exclusive in S1; "
               "disabling NZ in favour of W8\n");
        use_nz_weights_ = false;
    }
    const bool w8_enabled = use_w8_weights_ && g_cann.has_w8_quant() &&
                             !use_nz_weights_;
    if (use_w8_weights_ && !g_cann.has_w8_quant()) {
        printf("[cp_cann] W8 quant requested but "
               "aclnnWeightQuantBatchMatmulV3/V2 unresolved — "
               "falling back to F16\n");
    }
    const bool nz_enabled = use_nz_weights_ && g_cann.has_nz() && !w8_enabled;
    if (use_nz_weights_ && !g_cann.has_nz()) {
        printf("[cp_cann] NZ weights requested but "
               "aclnnTransMatmulWeight unresolved — falling back to ND\n");
    }
    if ((nz_enabled || w8_enabled) && workspace_dev_ == nullptr) {
        workspace_size_ = 4 * 1024 * 1024;
        ACL_CHECK_RET(g_cann.aclrtMalloc(&workspace_dev_, workspace_size_,
                                          ACL_MEM_MALLOC_HUGE_FIRST));
    }

    // Helper for W8 calibration in the safetensors path. We already have the
    // BF16 host buffer inline — reload as F32 for the scale computation.
    auto calibrate_w8 = [&](const std::string &tname,
                             int64_t rows, int64_t cols,
                             void *&w_i8_dev, void *&scale_dev) -> bool {
        StEntry e;
        if (!st_lookup(header, tname, e)) return false;
        size_t n = (size_t)rows * cols;
        if (e.data_end - e.data_start != n * 2) return false;
        std::vector<uint16_t> bf16_buf(n);
        f.seekg(data_base + (std::streamoff)e.data_start);
        f.read(reinterpret_cast<char *>(bf16_buf.data()), n * 2);
        std::vector<float> f32(n);
        for (size_t i = 0; i < n; ++i) {
            union { uint32_t u; float f; } v;
            v.u = ((uint32_t)bf16_buf[i]) << 16;
            f32[i] = v.f;
        }
        return w8_calibrate_weight_(f32.data(), rows, cols,
                                     w_i8_dev, scale_dev);
    };

    // Per-layer weights.
    layer_w_.resize(n_layers_);
    for (int il = 0; il < n_layers_; ++il) {
        auto &dst = layer_w_[il];
        const std::string lp = mpfx + "layers." + std::to_string(il) + ".";
        if (!load_one_f16(lp + "self_attn.q_proj.weight", dst.q_proj_w,
                          (size_t)q_dim_ * cp_hidden_)) return false;
        if (!load_one_f16(lp + "self_attn.k_proj.weight", dst.k_proj_w,
                          (size_t)kv_dim_ * cp_hidden_)) return false;
        if (!load_one_f16(lp + "self_attn.v_proj.weight", dst.v_proj_w,
                          (size_t)kv_dim_ * cp_hidden_)) return false;
        if (!load_one_f16(lp + "self_attn.o_proj.weight", dst.o_proj_w,
                          (size_t)cp_hidden_ * q_dim_)) return false;
        if (!load_one_f16(lp + "mlp.gate_proj.weight", dst.gate_proj_w,
                          (size_t)inter_ * cp_hidden_)) return false;
        if (!load_one_f16(lp + "mlp.up_proj.weight", dst.up_proj_w,
                          (size_t)inter_ * cp_hidden_)) return false;
        if (!load_one_f16(lp + "mlp.down_proj.weight", dst.down_proj_w,
                          (size_t)cp_hidden_ * inter_)) return false;
        if (nz_enabled) {
            nz_convert_weight_(dst.q_proj_w,    q_dim_,     cp_hidden_);
            nz_convert_weight_(dst.k_proj_w,    kv_dim_,    cp_hidden_);
            nz_convert_weight_(dst.v_proj_w,    kv_dim_,    cp_hidden_);
            nz_convert_weight_(dst.o_proj_w,    cp_hidden_, q_dim_);
            nz_convert_weight_(dst.gate_proj_w, inter_,     cp_hidden_);
            nz_convert_weight_(dst.up_proj_w,   inter_,     cp_hidden_);
            nz_convert_weight_(dst.down_proj_w, cp_hidden_, inter_);
        }
        if (w8_enabled) {
            if (!calibrate_w8(lp + "self_attn.q_proj.weight",
                               q_dim_, cp_hidden_,
                               dst.q_proj_w_i8, dst.q_proj_scale)) return false;
            if (!calibrate_w8(lp + "self_attn.k_proj.weight",
                               kv_dim_, cp_hidden_,
                               dst.k_proj_w_i8, dst.k_proj_scale)) return false;
            if (!calibrate_w8(lp + "self_attn.v_proj.weight",
                               kv_dim_, cp_hidden_,
                               dst.v_proj_w_i8, dst.v_proj_scale)) return false;
            if (!calibrate_w8(lp + "self_attn.o_proj.weight",
                               cp_hidden_, q_dim_,
                               dst.o_proj_w_i8, dst.o_proj_scale)) return false;
            if (!calibrate_w8(lp + "mlp.gate_proj.weight",
                               inter_, cp_hidden_,
                               dst.gate_proj_w_i8, dst.gate_proj_scale))
                return false;
            if (!calibrate_w8(lp + "mlp.up_proj.weight",
                               inter_, cp_hidden_,
                               dst.up_proj_w_i8, dst.up_proj_scale)) return false;
            if (!calibrate_w8(lp + "mlp.down_proj.weight",
                               cp_hidden_, inter_,
                               dst.down_proj_w_i8, dst.down_proj_scale))
                return false;
        }
        // Norms kept F32 (matches the F32 norm path in v9/v11).
        if (!load_one_f32(lp + "self_attn.q_norm.weight", dst.q_norm_w,
                          head_dim_)) return false;
        if (!load_one_f32(lp + "self_attn.k_norm.weight", dst.k_norm_w,
                          head_dim_)) return false;
        if (!load_one_f32(lp + "input_layernorm.weight", dst.input_ln_w,
                          cp_hidden_)) return false;
        if (!load_one_f32(lp + "post_attention_layernorm.weight",
                          dst.post_ln_w, cp_hidden_)) return false;
    }
    if (!load_one_f32(mpfx + "norm.weight", final_norm_w_dev_, cp_hidden_))
        return false;

    // --- The rest of init (buffers, scalars, cos/sin, persistent tensors,
    //     workspace) is identical to init(); reuse by setting up the shared
    //     tail. Factor it out to keep this maintainable. ---
    const size_t E = sizeof(uint16_t);

    alloc_dev(&cur_dev_,       cp_hidden_ * E);
    alloc_dev(&residual_dev_,  cp_hidden_ * E);
    alloc_dev(&normed_dev_,    cp_hidden_ * E);
    alloc_dev(&q_dev_,         q_dim_     * E);
    alloc_dev(&k_dev_,         kv_dim_    * E);
    alloc_dev(&v_dev_,         kv_dim_    * E);
    alloc_dev(&attn_out_dev_,  q_dim_     * E);
    alloc_dev(&o_out_dev_,     cp_hidden_ * E);
    alloc_dev(&gate_dev_,      inter_     * E);
    alloc_dev(&up_dev_,        inter_     * E);
    alloc_dev(&ffn_out_dev_,   cp_hidden_ * E);
    alloc_dev(&scores_dev_,    (size_t)n_heads_ * MAX_SEQ * sizeof(float));
    alloc_dev(&scores_f16_dev_,(size_t)n_heads_ * MAX_SEQ * E);
    alloc_dev(&rstd_dev_,      (size_t)n_heads_ * sizeof(float));
    alloc_dev(&input_stage_f32_dev_,  talker_hidden_ * sizeof(float));
    alloc_dev(&output_stage_f32_dev_, cp_hidden_     * sizeof(float));
    alloc_dev(&proj_out_f32_dev_,     cp_hidden_     * sizeof(float));
    alloc_dev(&accum_scratch_f32_dev_, cp_hidden_    * sizeof(float));
    alloc_dev(&normed_f32_dev_,        cp_hidden_    * sizeof(float));

    {
        const int half = head_dim_ / 2;
        std::vector<float> cos_table((size_t)MAX_SEQ * head_dim_);
        std::vector<float> sin_table((size_t)MAX_SEQ * head_dim_);
        for (int p = 0; p < MAX_SEQ; ++p) {
            for (int j = 0; j < half; ++j) {
                float freq  = 1.0f / powf(rope_theta_, (float)(2 * j) / head_dim_);
                float angle = (float)p * freq;
                float c = cosf(angle);
                float s = sinf(angle);
                cos_table[(size_t)p * head_dim_ + j]        = c;
                cos_table[(size_t)p * head_dim_ + j + half] = c;
                sin_table[(size_t)p * head_dim_ + j]        = s;
                sin_table[(size_t)p * head_dim_ + j + half] = s;
            }
        }
        alloc_dev(&rope_cos_dev_, (size_t)MAX_SEQ * head_dim_ * E);
        alloc_dev(&rope_sin_dev_, (size_t)MAX_SEQ * head_dim_ * E);
        upload_f16(rope_cos_dev_, cos_table.data(),
                   (size_t)MAX_SEQ * head_dim_);
        upload_f16(rope_sin_dev_, sin_table.data(),
                   (size_t)MAX_SEQ * head_dim_);
    }

    k_cache_dev_.resize(n_layers_, nullptr);
    v_cache_dev_.resize(n_layers_, nullptr);
    for (int il = 0; il < n_layers_; ++il) {
        alloc_dev(&k_cache_dev_[il], (size_t)MAX_SEQ * kv_dim_ * E);
        alloc_dev(&v_cache_dev_[il], (size_t)MAX_SEQ * kv_dim_ * E);
    }
    kv_cache_len_ = 0;

    {
        float scale_val = 1.0f / sqrtf((float)head_dim_);
        attn_scale_     = g_cann.aclCreateScalar(&scale_val, ACL_FLOAT);
    }
    attn_scale_f16_ = make_f16_scalar(1.0f / sqrtf((float)head_dim_));
    one_scalar_     = make_f16_scalar(1.0f);

    // Skip the seed alloc if NZ pre-conversion already set it up.
    if (workspace_dev_ == nullptr) {
        workspace_size_ = 4 * 1024 * 1024;
        ACL_CHECK_RET(g_cann.aclrtMalloc(&workspace_dev_, workspace_size_,
                                          ACL_MEM_MALLOC_HUGE_FIRST));
    }
    nz_applied_ = nz_enabled;
    w8_applied_ = w8_enabled;
    assert(!(w8_applied_ && nz_applied_));
    if (w8_applied_) {
        printf("[cp_cann] A16W8 weight quantization ENABLED (decode matmuls "
               "dispatch aclnnWeightQuantBatchMatmulV3/V2)\n");
    }

    // W3b: opt-in CP kernel fusion. Gated behind TALKER_CP_FUSION so the
    // default path is bit-identical to W1. When env is set AND the runtime
    // exposes aclnnAddRmsNorm, the per-sublayer `Add + RmsNorm` tail collapses
    // into one dispatch.
    {
        const char *env = getenv("TALKER_CP_FUSION");
        if (env && env[0] != '\0' && env[0] != '0') {
            cp_fusion_enabled_ = true;
        }
    }
    cp_fusion_applied_ = cp_fusion_enabled_ && g_cann.has_add_rms_norm();
    if (cp_fusion_enabled_ && !g_cann.has_add_rms_norm()) {
        printf("[cp_cann] TALKER_CP_FUSION requested but aclnnAddRmsNorm "
               "symbol absent on this CANN toolkit — staying on unfused "
               "Add + RmsNorm path\n");
    }
    init_fusion_f16_gammas_();
    if (cp_fusion_applied_) {
        printf("[cp_cann] CP kernel fusion ENABLED (aclnnAddRmsNorm replaces "
               "per-sublayer Add+RmsNorm tail; W3b)\n");
    }

    // Phase A.1: opt-in aclnnInplaceAddRmsNorm. Skips the trailing residual-
    // copy after each fused tail by having the in-place variant write the
    // sum into the residual tensor directly. Requires cp_fusion_applied_
    // (shares the fusion call sites). Default off; caller sets
    // TALKER_CP_INPLACE_ADDRMSNORM=1 to opt in.
    {
        const char *env = getenv("TALKER_CP_INPLACE_ADDRMSNORM");
        if (env && env[0] != '\0' && env[0] != '0') {
            cp_inplace_ars_enabled_ = true;
        }
    }
    cp_inplace_ars_applied_ = cp_inplace_ars_enabled_ &&
                              cp_fusion_applied_ &&
                              g_cann.has_inplace_add_rms_norm();
    if (cp_inplace_ars_enabled_ && !cp_fusion_applied_) {
        printf("[cp_cann] TALKER_CP_INPLACE_ADDRMSNORM requested but "
               "cp_fusion is not active — needs TALKER_CP_FUSION=1 to share "
               "the fused tail call sites. Staying on aclnnAddRmsNorm.\n");
    }
    if (cp_inplace_ars_enabled_ && cp_fusion_applied_ &&
        !g_cann.has_inplace_add_rms_norm()) {
        printf("[cp_cann] TALKER_CP_INPLACE_ADDRMSNORM requested but "
               "aclnnInplaceAddRmsNorm symbol absent — staying on the "
               "non-inplace path.\n");
    }
    if (cp_inplace_ars_applied_) {
        printf("[cp_cann] Phase A.1 ENABLED: aclnnInplaceAddRmsNorm "
               "replaces aclnnAddRmsNorm + residual-copy at each fused tail.\n");
    }

    // Phase B: opt-in aclnnFFNV3. Requires w8_applied_ (the fused kernel
    // consumes the A16W8 antiquant scale path) and the runtime symbol. Builds
    // the gate||up concatenated INT8 + F16 scale blobs per layer at init.
    {
        const char *env = getenv("TALKER_CP_FFN_V3");
        if (env && env[0] != '\0' && env[0] != '0') {
            cp_ffn_v3_enabled_ = true;
        }
    }
    cp_ffn_v3_applied_ = cp_ffn_v3_enabled_ &&
                         w8_applied_ &&
                         g_cann.has_ffn_v3();
    if (cp_ffn_v3_enabled_ && !w8_applied_) {
        printf("[cp_cann] TALKER_CP_FFN_V3 requested but A16W8 is not "
               "active — aclnnFFNV3 requires INT8 weights + F16 antiquant "
               "scales. Staying on stock 5-op FFN chain.\n");
    }
    if (cp_ffn_v3_enabled_ && w8_applied_ && !g_cann.has_ffn_v3()) {
        printf("[cp_cann] TALKER_CP_FFN_V3 requested but aclnnFFNV3 symbol "
               "absent — staying on stock 5-op FFN chain.\n");
    }
    if (cp_ffn_v3_applied_) {
        if (!build_ffn_v3_weights_()) {
            printf("[cp_cann] build_ffn_v3_weights_ failed — falling back to "
                   "stock FFN chain.\n");
            cp_ffn_v3_applied_ = false;
        } else {
            printf("[cp_cann] Phase B ENABLED: aclnnFFNV3 (swiglu) replaces "
                   "the 5-op FFN chain (gate-Mm+up-Mm+SiLU+mul+down-Mm).\n");
        }
    }

    // Phase A.2: opt-in aclnnApplyRotaryPosEmbV2 (fused Q+K in-place RoPE).
    // Re-wire of the original A.2 that was closed with a 457-vs-434 frame
    // divergence; see docs/v2_rope_reopen_verdict.md — op-level numerics are
    // correct on our GQA shape, so the divergence was a WIRING bug. The
    // re-wire (this code) applies five hypothesis fixes:
    //   1. Workspace via ensure_workspace (WSPOOL retain-list safe).
    //   2. Descriptor strides mirror v1's t_.q_rope_4d / t_k_rope_src
    //      exactly.
    //   3. aclGraph capture disabled while this flag is on (mirrors the A.1
    //      inplace-ARS gate in capture_aclgraph_forwards_()).
    //   4. V2 runs on the same stream as everything else — no event sync
    //      needed.
    //   5. K in-place target is k_cache_dev_[il] + pos * kv_dim_, which is
    //      the same slot v1 writes.
    {
        const char *env = getenv("TALKER_CP_ROPE_V2");
        if (env && env[0] != '\0' && env[0] != '0') {
            cp_rope_v2_enabled_ = true;
        }
    }
    cp_rope_v2_applied_ = cp_rope_v2_enabled_ && g_cann.has_rope_v2();
    if (cp_rope_v2_enabled_ && !g_cann.has_rope_v2()) {
        printf("[cp_cann] TALKER_CP_ROPE_V2 requested but "
               "aclnnApplyRotaryPosEmbV2 symbol absent — staying on the "
               "two-call aclnnRotaryPositionEmbedding path.\n");
    }
    if (cp_rope_v2_applied_) {
        printf("[cp_cann] Phase A.2 ENABLED: aclnnApplyRotaryPosEmbV2 "
               "(fused Q+K in-place RoPE) replaces the two NEOX RoPE "
               "calls.\n");
    }

    // Phase GMM-wire: opt-in aclnnGroupedMatmulV3 for Q/K/V. Requires
    // w8_applied_ (antiquantScale path) and the runtime symbol. Allocates a
    // shared zero-F16 antiquantOffset buffer of length q_dim_ (= max of the
    // three per-group Ns). See docs/qkv_grouped_probe_verdict.md for the
    // probe-verified A16W8 configuration (groupType=-1, splitItem=0,
    // antiquantOffset list of 3 zero views). Default off; caller sets
    // TALKER_CP_GMM_QKV=1 to opt in.
    {
        const char *env = getenv("TALKER_CP_GMM_QKV");
        if (env && env[0] != '\0' && env[0] != '0') {
            cp_gmm_qkv_enabled_ = true;
        }
    }
    cp_gmm_qkv_applied_ = cp_gmm_qkv_enabled_ &&
                          w8_applied_ &&
                          g_cann.has_grouped_matmul_v3();
    if (cp_gmm_qkv_enabled_ && !w8_applied_) {
        printf("[cp_cann] TALKER_CP_GMM_QKV requested but A16W8 is not "
               "active — aclnnGroupedMatmulV3 requires INT8 weights + F16 "
               "antiquant scales. Staying on 3× aclnnWeightQuantBatchMatmul.\n");
    }
    if (cp_gmm_qkv_enabled_ && w8_applied_ && !g_cann.has_grouped_matmul_v3()) {
        printf("[cp_cann] TALKER_CP_GMM_QKV requested but "
               "aclnnGroupedMatmulV3 symbol absent — staying on 3× "
               "aclnnWeightQuantBatchMatmul.\n");
    }
    if (cp_gmm_qkv_applied_) {
        // Allocate + zero-fill the shared F16 antiquantOffset buffer. Size
        // q_dim_ (largest of Nq/Nk/Nv); K/V groups wrap prefix views.
        alloc_dev(&antiquant_zero_offset_dev_,
                  (size_t)q_dim_ * sizeof(uint16_t));
        std::vector<uint16_t> zeros((size_t)q_dim_, 0);
        ACL_CHECK_RET(g_cann.aclrtMemcpy(
            antiquant_zero_offset_dev_, (size_t)q_dim_ * sizeof(uint16_t),
            zeros.data(), (size_t)q_dim_ * sizeof(uint16_t),
            ACL_MEMCPY_HOST_TO_DEVICE));
        printf("[cp_cann] Phase GMM-wire ENABLED: aclnnGroupedMatmulV3 "
               "replaces the 3 per-layer QKV w8_matmul_ calls (projected "
               "+0.3 fps; ~1.7e-3 relative drift vs 3-call reference).\n");
    }

    // W4.1: opt-in AscendC fused attention sublayer. Requires
    // QWEN_TTS_HAS_ASCENDC (build-time) AND w8_applied_ (weights in INT8
    // layout the kernel consumes) AND TALKER_CP_ASCENDC=1 (runtime gate).
    // Unset = bit-identical W1+W3b path.
    {
        const char *env = getenv("TALKER_CP_ASCENDC");
        if (env && env[0] != '\0' && env[0] != '0') {
            cp_ascendc_enabled_ = true;
        }
    }
#ifdef QWEN_TTS_HAS_ASCENDC
    cp_ascendc_applied_ = cp_ascendc_enabled_ && w8_applied_;
    if (cp_ascendc_enabled_ && !w8_applied_) {
        printf("[cp_cann] TALKER_CP_ASCENDC requested but A16W8 (W8) path "
               "is not active — the fused kernel consumes INT8 weights. "
               "Staying on stock aclnn attention chain.\n");
    }
#else
    if (cp_ascendc_enabled_) {
        printf("[cp_cann] TALKER_CP_ASCENDC requested but this build was "
               "compiled without QWEN_TTS_ASCENDC=ON — ignoring.\n");
    }
#endif
    init_ascendc_f16_gammas_();
    if (cp_ascendc_applied_) {
        printf("[cp_cann] W4.1 AscendC fused attn-sublayer kernel ENABLED "
               "(aclrtlaunch_fused_attn_sublayer replaces 12-op attn chain)\n");
    }

    build_persistent_tensors_();

    ready_ = true;

    // G2: opt-in aclGraph capture-at-init. Gated by TALKER_CP_ACLGRAPH=1.
    // Requires the canonical W8+fusion path to be active (capturable variant
    // handles only that combination). Silent no-op when unset.
    {
        const char *env = getenv("TALKER_CP_ACLGRAPH");
        aclgraph_enabled_ = (env && env[0] != '\0' && env[0] != '0');
        if (aclgraph_enabled_) {
            printf("[cp_cann] TALKER_CP_ACLGRAPH=1 — attempting full-forward "
                   "capture at init (pos 0..%d)...\n", MAX_ACLGRAPH_POS);
            capture_aclgraph_forwards_();
            if (aclgraph_applied_) {
                printf("[cp_cann] G2 aclGraph ENABLED — replay path active for "
                       "pos in [0, %d].\n", MAX_ACLGRAPH_POS);
            } else {
                printf("[cp_cann] G2 aclGraph disabled (capture incomplete) — "
                       "staying on eager path.\n");
            }
        }
    }

    printf("[cp_cann] BF16-weights engine initialized from %s\n", path.c_str());
    printf("[cp_cann] dims: cp_hidden=%d q_dim=%d kv_dim=%d inter=%d head=%d\n",
           cp_hidden_, q_dim_, kv_dim_, inter_, head_dim_);
    return true;
}

// ============================================================================
// init — allocate F16 buffers, convert weights F32->F16 at upload.
// ============================================================================

bool CpCannEngine::init(const CpWeightsF32 &w, const CodePredictorConfig &cfg,
                        int device) {
    if (!cp_cann_load_symbols()) {
        fprintf(stderr, "[cp_cann] symbol load failed; engine disabled\n");
        return false;
    }

    device_ = device;
    ACL_CHECK_RET(g_cann.aclrtSetDevice(device_));
    // Primary + secondary streams (M6.1). Primary is the default target;
    // stream_b_ is spare for multi-stream pipelining.
    ACL_CHECK_RET(g_cann.aclrtCreateStream(&primary_stream_));
    stream_ = primary_stream_;
    ACL_CHECK_RET(g_cann.aclrtCreateStream(&stream_b_));
    // M6.2: reusable event recorded at the end of forward_one_token_launch.
    if (g_cann.aclrtCreateEvent && !forward_done_event_) {
        ACL_CHECK_RET(g_cann.aclrtCreateEvent(&forward_done_event_));
    }

    talker_hidden_ = cfg.talker_hidden_size;
    cp_hidden_     = cfg.hidden_size;
    n_heads_       = cfg.num_attention_heads;
    n_kv_          = cfg.num_key_value_heads;
    head_dim_      = cfg.head_dim;
    q_dim_         = n_heads_ * head_dim_;
    kv_dim_        = n_kv_ * head_dim_;
    inter_         = cfg.intermediate_size;
    n_layers_      = cfg.num_hidden_layers;
    eps_           = cfg.rms_norm_eps;
    rope_theta_    = cfg.rope_theta;

    // All device buffers are F16 (2 bytes/elem).
    const size_t E = sizeof(uint16_t);

    // --- Upload projection weights as F32 (matches llama.cpp's CP path,
    //    which does the input projection on CPU with F32 precision). This
    //    is the single most precision-sensitive matmul in the CP because it
    //    converts between talker-space (high variance) and cp-space. ---
    alloc_dev(&proj_w_dev_,
              (size_t)cp_hidden_ * talker_hidden_ * sizeof(float));
    upload(proj_w_dev_, w.proj_w.data(),
           (size_t)cp_hidden_ * talker_hidden_);
    alloc_dev(&proj_b_dev_, cp_hidden_ * sizeof(float));
    upload(proj_b_dev_, w.proj_b.data(), cp_hidden_);

    // ---- FRACTAL_NZ / A16W8 gating (M5.2 / Stretch S1) ----------------------
    {
        const char *w8_env = getenv("TALKER_W8_QUANT");
        if (!use_w8_weights_ && w8_env && w8_env[0] != '\0' && w8_env[0] != '0') {
            use_w8_weights_ = true;
            printf("[cp_cann] TALKER_W8_QUANT=%s forcing A16W8 weight path\n",
                   w8_env);
        }
    }
    if (use_w8_weights_ && use_nz_weights_) {
        printf("[cp_cann] W8 and NZ mutually exclusive in S1; disabling NZ\n");
        use_nz_weights_ = false;
    }
    const bool w8_enabled = use_w8_weights_ && g_cann.has_w8_quant() &&
                             !use_nz_weights_;
    if (use_w8_weights_ && !g_cann.has_w8_quant()) {
        printf("[cp_cann] W8 quant requested but unresolved — falling back "
               "to F16\n");
    }
    const bool nz_enabled = use_nz_weights_ && g_cann.has_nz() && !w8_enabled;
    if (use_nz_weights_ && !g_cann.has_nz()) {
        printf("[cp_cann] NZ weights requested but "
               "aclnnTransMatmulWeight unresolved — falling back to ND\n");
    }
    // Seed workspace early so nz_convert_weight_ / w8 calib have scratch.
    if ((nz_enabled || w8_enabled) && workspace_dev_ == nullptr) {
        workspace_size_ = 4 * 1024 * 1024;
        ACL_CHECK_RET(g_cann.aclrtMalloc(&workspace_dev_, workspace_size_,
                                          ACL_MEM_MALLOC_HUGE_FIRST));
    }

    layer_w_.resize(n_layers_);
    auto upload_mat_f16 = [&](void *&dev, const std::vector<float> &host,
                               int rows, int cols) {
        alloc_dev(&dev, (size_t)rows * cols * E);
        upload_f16(dev, host.data(), (size_t)rows * cols);
    };
    for (int il = 0; il < n_layers_; ++il) {
        const auto &src = w.layers[il];
        auto &dst = layer_w_[il];
        upload_mat_f16(dst.q_proj_w,    src.q_proj_w,    q_dim_,     cp_hidden_);
        upload_mat_f16(dst.k_proj_w,    src.k_proj_w,    kv_dim_,    cp_hidden_);
        upload_mat_f16(dst.v_proj_w,    src.v_proj_w,    kv_dim_,    cp_hidden_);
        upload_mat_f16(dst.o_proj_w,    src.o_proj_w,    cp_hidden_, q_dim_);
        upload_mat_f16(dst.gate_proj_w, src.gate_proj_w, inter_,     cp_hidden_);
        upload_mat_f16(dst.up_proj_w,   src.up_proj_w,   inter_,     cp_hidden_);
        upload_mat_f16(dst.down_proj_w, src.down_proj_w, cp_hidden_, inter_);
        if (nz_enabled) {
            // proj_w / proj_b are F32 (F32 input projection path); keep those
            // in ND since TransMatmulWeight doesn't support F32 weights.
            nz_convert_weight_(dst.q_proj_w,    q_dim_,     cp_hidden_);
            nz_convert_weight_(dst.k_proj_w,    kv_dim_,    cp_hidden_);
            nz_convert_weight_(dst.v_proj_w,    kv_dim_,    cp_hidden_);
            nz_convert_weight_(dst.o_proj_w,    cp_hidden_, q_dim_);
            nz_convert_weight_(dst.gate_proj_w, inter_,     cp_hidden_);
            nz_convert_weight_(dst.up_proj_w,   inter_,     cp_hidden_);
            nz_convert_weight_(dst.down_proj_w, cp_hidden_, inter_);
        }
        if (w8_enabled) {
            if (!w8_calibrate_weight_(src.q_proj_w.data(), q_dim_, cp_hidden_,
                                       dst.q_proj_w_i8, dst.q_proj_scale))
                return false;
            if (!w8_calibrate_weight_(src.k_proj_w.data(), kv_dim_, cp_hidden_,
                                       dst.k_proj_w_i8, dst.k_proj_scale))
                return false;
            if (!w8_calibrate_weight_(src.v_proj_w.data(), kv_dim_, cp_hidden_,
                                       dst.v_proj_w_i8, dst.v_proj_scale))
                return false;
            if (!w8_calibrate_weight_(src.o_proj_w.data(), cp_hidden_, q_dim_,
                                       dst.o_proj_w_i8, dst.o_proj_scale))
                return false;
            if (!w8_calibrate_weight_(src.gate_proj_w.data(), inter_, cp_hidden_,
                                       dst.gate_proj_w_i8, dst.gate_proj_scale))
                return false;
            if (!w8_calibrate_weight_(src.up_proj_w.data(), inter_, cp_hidden_,
                                       dst.up_proj_w_i8, dst.up_proj_scale))
                return false;
            if (!w8_calibrate_weight_(src.down_proj_w.data(), cp_hidden_, inter_,
                                       dst.down_proj_w_i8, dst.down_proj_scale))
                return false;
        }
        // RMSNorm gammas are stored as F32 in the GGUF and aclnnRmsNorm
        // natively accepts F32 gamma with F16 input (SupportInfo[0]).
        // Casting them to F16 loses precision for a 1-in-1024-sized vector
        // that gets applied 5+ times per decode — noticeable in output.
        alloc_dev(&dst.q_norm_w, head_dim_ * sizeof(float));
        upload(dst.q_norm_w, src.q_norm_w.data(), head_dim_);
        alloc_dev(&dst.k_norm_w, head_dim_ * sizeof(float));
        upload(dst.k_norm_w, src.k_norm_w.data(), head_dim_);
        alloc_dev(&dst.input_ln_w, cp_hidden_ * sizeof(float));
        upload(dst.input_ln_w, src.input_ln_w.data(), cp_hidden_);
        alloc_dev(&dst.post_ln_w, cp_hidden_ * sizeof(float));
        upload(dst.post_ln_w, src.post_ln_w.data(), cp_hidden_);
    }
    alloc_dev(&final_norm_w_dev_, cp_hidden_ * sizeof(float));
    upload(final_norm_w_dev_, w.norm_w.data(), cp_hidden_);

    // --- Fixed-size working buffers (F16) ---
    alloc_dev(&cur_dev_,       cp_hidden_ * E);
    alloc_dev(&residual_dev_,  cp_hidden_ * E);
    alloc_dev(&normed_dev_,    cp_hidden_ * E);
    alloc_dev(&q_dev_,         q_dim_     * E);  // also F16 input stage post-cast (q_dim ≥ talker_hidden)
    alloc_dev(&k_dev_,         kv_dim_    * E);
    alloc_dev(&v_dev_,         kv_dim_    * E);
    alloc_dev(&attn_out_dev_,  q_dim_     * E);
    alloc_dev(&o_out_dev_,     cp_hidden_ * E);
    alloc_dev(&gate_dev_,      inter_     * E);
    alloc_dev(&up_dev_,        inter_     * E);
    alloc_dev(&ffn_out_dev_,   cp_hidden_ * E);

    // Attention scores buffer — kept F32 so softmax can run in F32 (matches
    // the precision of llama.cpp's aclnnFusedInferAttentionScoreV2 internal
    // softmax, which the user-audible F16-throughout softmax can't replicate).
    alloc_dev(&scores_dev_, (size_t)n_heads_ * MAX_SEQ * sizeof(float));
    // Separate F16 scratch for the attn_weights BMM input (F16 required).
    alloc_dev(&scores_f16_dev_, (size_t)n_heads_ * MAX_SEQ * E);
    // rstd is F32 (aclnnRmsNorm spec); sized for the largest case (n_heads).
    alloc_dev(&rstd_dev_,   (size_t)n_heads_ * sizeof(float));

    // Boundary F32 staging buffers
    alloc_dev(&input_stage_f32_dev_,  talker_hidden_ * sizeof(float));
    alloc_dev(&output_stage_f32_dev_, cp_hidden_     * sizeof(float));
    // F32 scratch for the input-projection output AND the running F32
    // residual accumulator used through all transformer layers.
    alloc_dev(&proj_out_f32_dev_,     cp_hidden_     * sizeof(float));
    alloc_dev(&accum_scratch_f32_dev_, cp_hidden_    * sizeof(float));
    alloc_dev(&normed_f32_dev_,        cp_hidden_    * sizeof(float));

    // --- Precompute cos/sin tables as F16 ---
    {
        const int half = head_dim_ / 2;
        std::vector<float> cos_table((size_t)MAX_SEQ * head_dim_);
        std::vector<float> sin_table((size_t)MAX_SEQ * head_dim_);
        for (int p = 0; p < MAX_SEQ; ++p) {
            for (int j = 0; j < half; ++j) {
                float freq  = 1.0f / powf(rope_theta_, (float)(2 * j) / head_dim_);
                float angle = (float)p * freq;
                float c = cosf(angle);
                float s = sinf(angle);
                cos_table[(size_t)p * head_dim_ + j]        = c;
                cos_table[(size_t)p * head_dim_ + j + half] = c;
                sin_table[(size_t)p * head_dim_ + j]        = s;
                sin_table[(size_t)p * head_dim_ + j + half] = s;
            }
        }
        alloc_dev(&rope_cos_dev_, (size_t)MAX_SEQ * head_dim_ * E);
        alloc_dev(&rope_sin_dev_, (size_t)MAX_SEQ * head_dim_ * E);
        upload_f16(rope_cos_dev_, cos_table.data(), (size_t)MAX_SEQ * head_dim_);
        upload_f16(rope_sin_dev_, sin_table.data(), (size_t)MAX_SEQ * head_dim_);
    }

    // --- KV cache (F16) ---
    k_cache_dev_.resize(n_layers_, nullptr);
    v_cache_dev_.resize(n_layers_, nullptr);
    for (int il = 0; il < n_layers_; ++il) {
        alloc_dev(&k_cache_dev_[il], (size_t)MAX_SEQ * kv_dim_ * E);
        alloc_dev(&v_cache_dev_[il], (size_t)MAX_SEQ * kv_dim_ * E);
    }
    kv_cache_len_ = 0;

    // --- Scalars: F32 attn_scale for the F32 softmax path; F16 one_scalar
    //    for Add/InplaceAdd on F16 tensors. ---
    {
        float scale_val = 1.0f / sqrtf((float)head_dim_);
        attn_scale_     = g_cann.aclCreateScalar(&scale_val, ACL_FLOAT);
    }
    attn_scale_f16_ = make_f16_scalar(1.0f / sqrtf((float)head_dim_));
    one_scalar_     = make_f16_scalar(1.0f);

    // --- Workspace seed ---
    // Skip if the NZ pre-conversion path already allocated and possibly
    // grew the workspace earlier; we don't want to leak that buffer.
    if (workspace_dev_ == nullptr) {
        workspace_size_ = 4 * 1024 * 1024;
        ACL_CHECK_RET(g_cann.aclrtMalloc(&workspace_dev_, workspace_size_,
                                          ACL_MEM_MALLOC_HUGE_FIRST));
    }
    nz_applied_ = nz_enabled;
    w8_applied_ = w8_enabled;
    assert(!(w8_applied_ && nz_applied_));
    if (w8_applied_) {
        printf("[cp_cann] A16W8 weight quantization ENABLED (decode matmuls "
               "dispatch aclnnWeightQuantBatchMatmulV3/V2)\n");
    }

    // W3b: CP kernel fusion detection. Must run BEFORE build_persistent_tensors_
    // so the F16 gamma tensors are created when fusion is applied. See
    // init_from_safetensors() for rationale.
    {
        const char *env = getenv("TALKER_CP_FUSION");
        if (env && env[0] != '\0' && env[0] != '0') {
            cp_fusion_enabled_ = true;
        }
    }
    cp_fusion_applied_ = cp_fusion_enabled_ && g_cann.has_add_rms_norm();
    if (cp_fusion_enabled_ && !g_cann.has_add_rms_norm()) {
        printf("[cp_cann] TALKER_CP_FUSION requested but aclnnAddRmsNorm "
               "symbol absent on this CANN toolkit — staying on unfused "
               "Add + RmsNorm path\n");
    }
    init_fusion_f16_gammas_();
    if (cp_fusion_applied_) {
        printf("[cp_cann] CP kernel fusion ENABLED (aclnnAddRmsNorm replaces "
               "per-sublayer Add+RmsNorm tail; W3b)\n");
    }

    // Phase A.1: opt-in aclnnInplaceAddRmsNorm (mirror of the init() path).
    {
        const char *env = getenv("TALKER_CP_INPLACE_ADDRMSNORM");
        if (env && env[0] != '\0' && env[0] != '0') {
            cp_inplace_ars_enabled_ = true;
        }
    }
    cp_inplace_ars_applied_ = cp_inplace_ars_enabled_ &&
                              cp_fusion_applied_ &&
                              g_cann.has_inplace_add_rms_norm();
    if (cp_inplace_ars_enabled_ && !cp_fusion_applied_) {
        printf("[cp_cann] TALKER_CP_INPLACE_ADDRMSNORM requested but "
               "cp_fusion is not active — needs TALKER_CP_FUSION=1 to share "
               "the fused tail call sites. Staying on aclnnAddRmsNorm.\n");
    }
    if (cp_inplace_ars_enabled_ && cp_fusion_applied_ &&
        !g_cann.has_inplace_add_rms_norm()) {
        printf("[cp_cann] TALKER_CP_INPLACE_ADDRMSNORM requested but "
               "aclnnInplaceAddRmsNorm symbol absent — staying on the "
               "non-inplace path.\n");
    }
    if (cp_inplace_ars_applied_) {
        printf("[cp_cann] Phase A.1 ENABLED: aclnnInplaceAddRmsNorm "
               "replaces aclnnAddRmsNorm + residual-copy at each fused tail.\n");
    }

    // Phase B: opt-in aclnnFFNV3. Requires w8_applied_ (the fused kernel
    // consumes the A16W8 antiquant scale path) and the runtime symbol. Builds
    // the gate||up concatenated INT8 + F16 scale blobs per layer at init.
    {
        const char *env = getenv("TALKER_CP_FFN_V3");
        if (env && env[0] != '\0' && env[0] != '0') {
            cp_ffn_v3_enabled_ = true;
        }
    }
    cp_ffn_v3_applied_ = cp_ffn_v3_enabled_ &&
                         w8_applied_ &&
                         g_cann.has_ffn_v3();
    if (cp_ffn_v3_enabled_ && !w8_applied_) {
        printf("[cp_cann] TALKER_CP_FFN_V3 requested but A16W8 is not "
               "active — aclnnFFNV3 requires INT8 weights + F16 antiquant "
               "scales. Staying on stock 5-op FFN chain.\n");
    }
    if (cp_ffn_v3_enabled_ && w8_applied_ && !g_cann.has_ffn_v3()) {
        printf("[cp_cann] TALKER_CP_FFN_V3 requested but aclnnFFNV3 symbol "
               "absent — staying on stock 5-op FFN chain.\n");
    }
    if (cp_ffn_v3_applied_) {
        if (!build_ffn_v3_weights_()) {
            printf("[cp_cann] build_ffn_v3_weights_ failed — falling back to "
                   "stock FFN chain.\n");
            cp_ffn_v3_applied_ = false;
        } else {
            printf("[cp_cann] Phase B ENABLED: aclnnFFNV3 (swiglu) replaces "
                   "the 5-op FFN chain (gate-Mm+up-Mm+SiLU+mul+down-Mm).\n");
        }
    }

    // Phase A.2: opt-in aclnnApplyRotaryPosEmbV2 (fused Q+K in-place RoPE).
    // Re-wire of the original A.2 that was closed with a 457-vs-434 frame
    // divergence; see docs/v2_rope_reopen_verdict.md — op-level numerics are
    // correct on our GQA shape, so the divergence was a WIRING bug. The
    // re-wire (this code) applies five hypothesis fixes:
    //   1. Workspace via ensure_workspace (WSPOOL retain-list safe).
    //   2. Descriptor strides mirror v1's t_.q_rope_4d / t_k_rope_src
    //      exactly.
    //   3. aclGraph capture disabled while this flag is on (mirrors the A.1
    //      inplace-ARS gate in capture_aclgraph_forwards_()).
    //   4. V2 runs on the same stream as everything else — no event sync
    //      needed.
    //   5. K in-place target is k_cache_dev_[il] + pos * kv_dim_, which is
    //      the same slot v1 writes.
    {
        const char *env = getenv("TALKER_CP_ROPE_V2");
        if (env && env[0] != '\0' && env[0] != '0') {
            cp_rope_v2_enabled_ = true;
        }
    }
    cp_rope_v2_applied_ = cp_rope_v2_enabled_ && g_cann.has_rope_v2();
    if (cp_rope_v2_enabled_ && !g_cann.has_rope_v2()) {
        printf("[cp_cann] TALKER_CP_ROPE_V2 requested but "
               "aclnnApplyRotaryPosEmbV2 symbol absent — staying on the "
               "two-call aclnnRotaryPositionEmbedding path.\n");
    }
    if (cp_rope_v2_applied_) {
        printf("[cp_cann] Phase A.2 ENABLED: aclnnApplyRotaryPosEmbV2 "
               "(fused Q+K in-place RoPE) replaces the two NEOX RoPE "
               "calls.\n");
    }

    // Phase GMM-wire: opt-in aclnnGroupedMatmulV3 (mirror of the
    // init_from_safetensors path above). Allocates the shared zero-F16
    // antiquantOffset buffer when the flag is on.
    {
        const char *env = getenv("TALKER_CP_GMM_QKV");
        if (env && env[0] != '\0' && env[0] != '0') {
            cp_gmm_qkv_enabled_ = true;
        }
    }
    cp_gmm_qkv_applied_ = cp_gmm_qkv_enabled_ &&
                          w8_applied_ &&
                          g_cann.has_grouped_matmul_v3();
    if (cp_gmm_qkv_enabled_ && !w8_applied_) {
        printf("[cp_cann] TALKER_CP_GMM_QKV requested but A16W8 is not "
               "active — aclnnGroupedMatmulV3 requires INT8 weights + F16 "
               "antiquant scales. Staying on 3× aclnnWeightQuantBatchMatmul.\n");
    }
    if (cp_gmm_qkv_enabled_ && w8_applied_ && !g_cann.has_grouped_matmul_v3()) {
        printf("[cp_cann] TALKER_CP_GMM_QKV requested but "
               "aclnnGroupedMatmulV3 symbol absent — staying on 3× "
               "aclnnWeightQuantBatchMatmul.\n");
    }
    if (cp_gmm_qkv_applied_) {
        alloc_dev(&antiquant_zero_offset_dev_,
                  (size_t)q_dim_ * sizeof(uint16_t));
        std::vector<uint16_t> zeros((size_t)q_dim_, 0);
        ACL_CHECK_RET(g_cann.aclrtMemcpy(
            antiquant_zero_offset_dev_, (size_t)q_dim_ * sizeof(uint16_t),
            zeros.data(), (size_t)q_dim_ * sizeof(uint16_t),
            ACL_MEMCPY_HOST_TO_DEVICE));
        printf("[cp_cann] Phase GMM-wire ENABLED: aclnnGroupedMatmulV3 "
               "replaces the 3 per-layer QKV w8_matmul_ calls (projected "
               "+0.3 fps; ~1.7e-3 relative drift vs 3-call reference).\n");
    }

    // W4.1: opt-in AscendC fused attention sublayer. Same env+build-gate
    // semantics as the init_from_safetensors path above.
    {
        const char *env = getenv("TALKER_CP_ASCENDC");
        if (env && env[0] != '\0' && env[0] != '0') {
            cp_ascendc_enabled_ = true;
        }
    }
#ifdef QWEN_TTS_HAS_ASCENDC
    cp_ascendc_applied_ = cp_ascendc_enabled_ && w8_applied_;
    if (cp_ascendc_enabled_ && !w8_applied_) {
        printf("[cp_cann] TALKER_CP_ASCENDC requested but A16W8 (W8) path "
               "is not active — the fused kernel consumes INT8 weights. "
               "Staying on stock aclnn attention chain.\n");
    }
#else
    if (cp_ascendc_enabled_) {
        printf("[cp_cann] TALKER_CP_ASCENDC requested but this build was "
               "compiled without QWEN_TTS_ASCENDC=ON — ignoring.\n");
    }
#endif
    init_ascendc_f16_gammas_();
    if (cp_ascendc_applied_) {
        printf("[cp_cann] W4.1 AscendC fused attn-sublayer kernel ENABLED "
               "(aclrtlaunch_fused_attn_sublayer replaces 12-op attn chain)\n");
    }

    build_persistent_tensors_();

    // W1: opt-in NPU lm_head port. Gated behind TALKER_LM_HEAD_NPU to keep
    // the default CPU matvec path bit-for-bit unchanged. Uploads F16 copies
    // of every per-group [vocab, cp_hidden] head weight and allocates two
    // small logits buffers (F16 on device, F32 staging for D2H).
    {
        const char *env = getenv("TALKER_LM_HEAD_NPU");
        if (env && env[0] != '\0' && env[0] != '0') {
            if (!init_lm_head_(w.lm_head_w, cfg.vocab_size)) {
                printf("[cp_cann] TALKER_LM_HEAD_NPU requested but lm_head "
                       "upload failed; caller should stay on CPU path\n");
            }
        }
    }

    ready_ = true;

    // G2: opt-in aclGraph capture-at-init (mirror of the init_from_safetensors
    // block). See forward_one_token_capturable_ for the capture-friendly
    // forward variant.
    {
        const char *env = getenv("TALKER_CP_ACLGRAPH");
        aclgraph_enabled_ = (env && env[0] != '\0' && env[0] != '0');
        if (aclgraph_enabled_) {
            printf("[cp_cann] TALKER_CP_ACLGRAPH=1 — attempting full-forward "
                   "capture at init (pos 0..%d)...\n", MAX_ACLGRAPH_POS);
            capture_aclgraph_forwards_();
            if (aclgraph_applied_) {
                printf("[cp_cann] G2 aclGraph ENABLED — replay path active for "
                       "pos in [0, %d].\n", MAX_ACLGRAPH_POS);
            } else {
                printf("[cp_cann] G2 aclGraph disabled (capture incomplete) — "
                       "staying on eager path.\n");
            }
        }
    }

    printf("[cp_cann] v4 F16 engine initialized: %d layers, device %d\n",
           n_layers_, device_);
    printf("[cp_cann] dims: cp_hidden=%d q_dim=%d kv_dim=%d inter=%d head=%d\n",
           cp_hidden_, q_dim_, kv_dim_, inter_, head_dim_);
    return true;
}

void CpCannEngine::reset_kv_cache() {
    kv_cache_len_ = 0;
}

// ============================================================================
// G2: forward_one_token_capturable_ — aclGraph-safe mirror of the W8+fusion
// canonical forward path. Differences from forward_one_token_launch eager:
//   * H2D input upload is NOT here — the caller stages into
//     input_stage_f32_dev_ before this is called (capture time) or before the
//     captured graph is replayed (runtime; handled by the aclgraph fast path
//     in forward_one_token_launch).
//   * D2D `aclrtMemcpyAsync` calls (residual=cur at layer-start, V→v_cache
//     slot, residual=cur after fused post-attn AddRmsNorm) are replaced with
//     `aclnnAdd(src, zero_f16_cp_dev_, alpha=1, dst)` for the residual copies
//     and with direct V-proj output redirection to the cache slot. Both are
//     capturable aclnn ops; `aclrtMemcpyAsync` D2D returns err=507009 inside
//     aclmdlRICapture on CANN 8.3.RC1 (G1 finding).
//   * Only handles the canonical W8+fusion path (cp_fusion_applied_ &&
//     w8_applied_ && !cp_ascendc_applied_). Callers must verify before
//     invoking; otherwise aclgraph stays disabled.
// ============================================================================

void CpCannEngine::forward_one_token_capturable_(int pos) {
    assert(ready_);
    assert(cp_fusion_applied_ && w8_applied_ && !cp_ascendc_applied_);
    assert(zero_f16_cp_dev_ != nullptr);

    // Helper: aclnn-based F16 copy buf-sized [cp_hidden]. dst = src + 1*zero.
    auto aclnn_copy_cp_ = [&](void *dst, void *src) {
        aclTensor *t_src = tensor_1d(src, cp_hidden_, ACL_FLOAT16);
        aclTensor *t_zero = tensor_1d(zero_f16_cp_dev_, cp_hidden_, ACL_FLOAT16);
        aclTensor *t_dst = tensor_1d(dst, cp_hidden_, ACL_FLOAT16);
        float one = 1.0f;
        aclScalar *alpha = g_cann.aclCreateScalar(&one, ACL_FLOAT16);
        // out = self + alpha * other -> dst = src + 1*zero = src.
        uint64_t ws = 0; aclOpExecutor *exec = nullptr;
        ACL_CHECK_RET(g_cann.aclnnAddGetWorkspaceSize(t_src, t_zero, alpha,
                                                       t_dst, &ws, &exec));
        ensure_workspace((size_t)ws);
        void *ws_ptr = ws > 0 ? workspace_dev_ : nullptr;
        ACL_CHECK_RET(g_cann.aclnnAdd(ws_ptr, ws, exec, stream_));
        g_cann.aclDestroyTensor(t_src);
        g_cann.aclDestroyTensor(t_zero);
        g_cann.aclDestroyTensor(t_dst);
        g_cann.aclDestroyScalar(alpha);
    };

    // Input projection in F32 (proj_w/proj_b are F32).
    {
        aclTensor *t_x = tensor_2d(input_stage_f32_dev_,
                                    talker_hidden_, 1, ACL_FLOAT);
        CANN_OP(Mm, t_.proj_w_f32, t_x, t_.proj_out_f32_col,
                /*cubeMathType=*/0);
        g_cann.aclDestroyTensor(t_x);
    }
    {
        float one = 1.0f;
        aclScalar *alpha_f32 = g_cann.aclCreateScalar(&one, ACL_FLOAT);
        CANN_OP(InplaceAdd, t_.proj_out_f32_flat, t_.proj_b_f32, alpha_f32);
        g_cann.aclDestroyScalar(alpha_f32);
    }
    CANN_OP(Cast, t_.proj_out_f32_flat, ACL_FLOAT16,
            t_.cur_f16_flat_as_target);

    const int seq_len = pos + 1;

    uint16_t *cos_pos = (uint16_t *)rope_cos_dev_ + (size_t)pos * head_dim_;
    uint16_t *sin_pos = (uint16_t *)rope_sin_dev_ + (size_t)pos * head_dim_;
    aclTensor *t_cos = [&]() {
        int64_t shape[4]   = {1, 1, 1, (int64_t)head_dim_};
        int64_t strides[4] = {(int64_t)head_dim_, (int64_t)head_dim_,
                               (int64_t)head_dim_, 1};
        return tensor_strided(cos_pos, 4, shape, strides);
    }();
    aclTensor *t_sin = [&]() {
        int64_t shape[4]   = {1, 1, 1, (int64_t)head_dim_};
        int64_t strides[4] = {(int64_t)head_dim_, (int64_t)head_dim_,
                               (int64_t)head_dim_, 1};
        return tensor_strided(sin_pos, 4, shape, strides);
    }();

    const int layers_to_run = (active_layers_ > 0 && active_layers_ < n_layers_)
                               ? active_layers_ : n_layers_;
    for (int il = 0; il < layers_to_run; ++il) {
        const auto &lt = t_.layer[il];

        // Layer-start: residual = cur (aclnn copy, not D2D memcpy).
        // Unlike the eager path, we execute this copy EVERY layer even in
        // fused mode — the prior layer's fused AddRmsNorm produces `cur`
        // holding the running residual sum, and this layer's post-attn Add
        // needs it mirrored into `residual_dev_`. In eager mode this was the
        // `aclrtMemcpyAsync(residual, cur, D2D)` call; aclnn Add does the
        // same without hitting the capture-incompatible memcpy primitive.
        //
        // Phase A.1: with inplace AddRmsNorm, residual_dev_ IS the x2Ref
        // output slot, so the per-layer residual-copy is eliminated. The
        // il==0 initial seeding still needs one copy from cur_dev_ because
        // the input-projection tail wrote the first hidden into cur_dev_.
        if (il == 0) {
            aclnn_copy_cp_(residual_dev_, cur_dev_);
            // Fused-path: the FIRST layer still needs the input_ln RmsNorm
            // (there's no prior fused Add to have produced normed_dev_).
            CANN_OP(RmsNorm, t_.cur_row, lt.input_ln, (double)eps_,
                    t_.normed_row, t_.rstd_11);
        } else if (!cp_inplace_ars_applied_) {
            // il > 0 in fused path: `normed_dev_` already holds
            // RmsNorm(cur, input_ln[il]) from prior layer's post-FFN fusion.
            // We still need residual=cur mirrored for THIS layer's post-attn.
            // Inplace path: skip — residual_dev_ already holds running sum.
            aclnn_copy_cp_(residual_dev_, cur_dev_);
        }

        // QKV projections (W8 path). V-proj writes DIRECTLY into the v_cache
        // slot for this (layer, pos), removing the need for a D2D memcpy
        // post-hoc. Since `pos` is baked per-captured-graph, the tensor
        // descriptor below locks in the right slot at capture time.
        //
        // GMM-wire variant: one aclnnGroupedMatmulV3 dispatch instead of
        // three w8_matmul_ calls. V destination is still the per-(il,pos)
        // cache slot — gmm_qkv_ takes a raw device pointer so the slot
        // address can be baked into the captured graph just like above.
        {
            uint16_t *v_slot_ptr =
                (uint16_t *)v_cache_dev_[il] + (size_t)pos * kv_dim_;
            if (cp_gmm_qkv_applied_) {
                gmm_qkv_(normed_dev_, il,
                          q_dev_, k_dev_, (void *)v_slot_ptr);
            } else {
                aclTensor *t_n_row = tensor_2d(normed_dev_, 1, cp_hidden_, ACL_FLOAT16);
                aclTensor *t_q_row = tensor_2d(q_dev_, 1, q_dim_, ACL_FLOAT16);
                aclTensor *t_k_row = tensor_2d(k_dev_, 1, kv_dim_, ACL_FLOAT16);
                // V-proj output view into v_cache_dev_[il] + pos*kv_dim (F16).
                aclTensor *t_v_row = tensor_2d(v_slot_ptr, 1, kv_dim_, ACL_FLOAT16);
                w8_matmul_(t_n_row, layer_w_[il].q_proj_w_i8,
                            layer_w_[il].q_proj_scale, q_dim_, cp_hidden_, t_q_row);
                w8_matmul_(t_n_row, layer_w_[il].k_proj_w_i8,
                            layer_w_[il].k_proj_scale, kv_dim_, cp_hidden_, t_k_row);
                w8_matmul_(t_n_row, layer_w_[il].v_proj_w_i8,
                            layer_w_[il].v_proj_scale, kv_dim_, cp_hidden_, t_v_row);
                g_cann.aclDestroyTensor(t_n_row);
                g_cann.aclDestroyTensor(t_q_row);
                g_cann.aclDestroyTensor(t_k_row);
                g_cann.aclDestroyTensor(t_v_row);
            }
        }

        // q_norm / k_norm (per-head / per-kv-head RmsNorm).
        CANN_OP(RmsNorm, t_.q_heads, lt.q_norm, (double)eps_,
                t_.q_heads, t_.rstd_heads);
        CANN_OP(RmsNorm, t_.k_kv,    lt.k_norm, (double)eps_,
                t_.k_kv,    t_.rstd_kv);

        // RoPE: either v1 two-call NEOX (stock) or V2 fused in-place (Phase A.2).
        // V2 path rewires Q to live at q_dev_ post-RoPE (in-place) rather than
        // at attn_out_dev_. FIAv2's Q source + output target swap accordingly,
        // and O-proj is redirected to read attn_out_dev_ below under the same
        // gate. Hypothesis-fix rationale in cp_cann_engine.h under
        // cp_rope_v2_applied_.
        uint16_t *k_cache_slot =
            (uint16_t *)k_cache_dev_[il] + (size_t)pos * kv_dim_;
        if (cp_rope_v2_applied_) {
            // Build Q and K tensor descriptors that MIRROR the v1 NEOX path
            // exactly: same shape, same strides, same dtype, same layout.
            // (Hypothesis-fix #2: descriptor parity.)
            aclTensor *t_q_rope_v2 = [&]() {
                int64_t shape[4]   = {1, 1, (int64_t)n_heads_, (int64_t)head_dim_};
                int64_t strides[4] = {(int64_t)q_dim_, (int64_t)q_dim_,
                                       (int64_t)head_dim_, 1};
                return tensor_strided(q_dev_, 4, shape, strides);
            }();
            aclTensor *t_k_rope_v2 = [&]() {
                int64_t shape[4]   = {1, 1, (int64_t)n_kv_, (int64_t)head_dim_};
                int64_t strides[4] = {(int64_t)kv_dim_, (int64_t)kv_dim_,
                                       (int64_t)head_dim_, 1};
                return tensor_strided(k_cache_slot, 4, shape, strides);
            }();
            // V2 GetWorkspaceSize takes non-const queryRef + keyRef since it
            // writes back into them in place. Invoke explicitly (not via
            // CANN_OP) because the GetWorkspaceSize arg order differs (layout
            // + rotaryMode before the ws pointer out-params). Workspace flows
            // through ensure_workspace() which IS WSPOOL retain-list safe.
            // (Hypothesis-fix #1: workspace lifetime.)
            uint64_t v2_ws = 0;
            aclOpExecutor *v2_exec = nullptr;
            char rotary_mode[5] = {'h','a','l','f',0};
            ACL_CHECK_RET(g_cann.aclnnApplyRotaryPosEmbV2GetWorkspaceSize(
                t_q_rope_v2, t_k_rope_v2, t_cos, t_sin,
                /*layout=*/(int64_t)1, rotary_mode, &v2_ws, &v2_exec));
            ensure_workspace((size_t)v2_ws);
            void *v2_ws_ptr = v2_ws > 0 ? workspace_dev_ : nullptr;
            ACL_CHECK_RET(g_cann.aclnnApplyRotaryPosEmbV2(
                v2_ws_ptr, v2_ws, v2_exec, stream_));
            g_cann.aclDestroyTensor(t_q_rope_v2);
            g_cann.aclDestroyTensor(t_k_rope_v2);
        } else {
            // v1 NEOX path: two aclnnRotaryPositionEmbedding calls.
            // Q RoPE (out -> attn_out_dev_).
            CANN_OP(RotaryPositionEmbedding,
                    t_.q_rope_4d, t_cos, t_sin,
                    /*mode=*/(int64_t)0, t_.attn_out_4d);

            // K RoPE: write directly into the KV-cache slot.
            aclTensor *t_k_rope_src = [&]() {
                int64_t shape[4]   = {1, 1, (int64_t)n_kv_, (int64_t)head_dim_};
                int64_t strides[4] = {(int64_t)kv_dim_, (int64_t)kv_dim_,
                                       (int64_t)head_dim_, 1};
                return tensor_strided(k_dev_, 4, shape, strides);
            }();
            aclTensor *t_k_rope_dst = [&]() {
                int64_t shape[4]   = {1, 1, (int64_t)n_kv_, (int64_t)head_dim_};
                int64_t strides[4] = {(int64_t)kv_dim_, (int64_t)kv_dim_,
                                       (int64_t)head_dim_, 1};
                return tensor_strided(k_cache_slot, 4, shape, strides);
            }();
            CANN_OP(RotaryPositionEmbedding,
                    t_k_rope_src, t_cos, t_sin,
                    /*mode=*/(int64_t)0, t_k_rope_dst);
            g_cann.aclDestroyTensor(t_k_rope_src);
            g_cann.aclDestroyTensor(t_k_rope_dst);
        }

        // V-to-cache-slot D2D memcpy is GONE — V-proj above wrote directly
        // into the slot. This is the key capture-friendly change.

        // FIAv2: seq_len baked per pos into the K/V stride-over-seq views.
        // Q source/output buffers SWAP under V2: v1 had RoPE'd Q at
        // attn_out_dev_ and FIA output at q_dev_; V2 leaves RoPE'd Q at
        // q_dev_ in-place, so FIA reads from q_dev_ and writes into
        // attn_out_dev_. O-proj reads from whichever buffer holds the FIA
        // output (see `attn_out_src` below).
        void *fia_q_src_dev = cp_rope_v2_applied_ ? q_dev_ : attn_out_dev_;
        void *fia_out_dst_dev = cp_rope_v2_applied_ ? attn_out_dev_ : q_dev_;
        aclTensor *t_q_bsnd = [&]() {
            int64_t shape[4]   = {1, 1, (int64_t)n_heads_, (int64_t)head_dim_};
            int64_t strides[4] = {(int64_t)q_dim_, (int64_t)q_dim_,
                                   (int64_t)head_dim_, 1};
            return tensor_strided(fia_q_src_dev, 4, shape, strides,
                                   ACL_FLOAT16);
        }();
        aclTensor *t_k_bsnd = [&]() {
            int64_t shape[4]   = {1, (int64_t)seq_len, (int64_t)n_kv_,
                                   (int64_t)head_dim_};
            int64_t strides[4] = {(int64_t)seq_len * kv_dim_,
                                   (int64_t)kv_dim_,
                                   (int64_t)head_dim_, 1};
            return tensor_strided(k_cache_dev_[il], 4, shape, strides,
                                   ACL_FLOAT16);
        }();
        aclTensor *t_v_bsnd = [&]() {
            int64_t shape[4]   = {1, (int64_t)seq_len, (int64_t)n_kv_,
                                   (int64_t)head_dim_};
            int64_t strides[4] = {(int64_t)seq_len * kv_dim_,
                                   (int64_t)kv_dim_,
                                   (int64_t)head_dim_, 1};
            return tensor_strided(v_cache_dev_[il], 4, shape, strides,
                                   ACL_FLOAT16);
        }();
        aclTensor *t_attn_out_bsnd = [&]() {
            int64_t shape[4]   = {1, 1, (int64_t)n_heads_, (int64_t)head_dim_};
            int64_t strides[4] = {(int64_t)q_dim_, (int64_t)q_dim_,
                                   (int64_t)head_dim_, 1};
            return tensor_strided(fia_out_dst_dev, 4, shape, strides,
                                   ACL_FLOAT16);
        }();

        aclTensorList *t_k_list = g_cann.aclCreateTensorList(&t_k_bsnd, 1);
        aclTensorList *t_v_list = g_cann.aclCreateTensorList(&t_v_bsnd, 1);

        {
            uint64_t fa_ws = 0;
            aclOpExecutor *fa_exec = nullptr;
            char layout[5] = {'B','S','N','D',0};
            double scale = 1.0 / sqrt((double)head_dim_);
            ACL_CHECK_RET(g_cann.aclnnFusedInferAttentionScoreV2GetWorkspaceSize(
                t_q_bsnd, t_k_list, t_v_list,
                nullptr, nullptr, nullptr, nullptr, nullptr, nullptr,
                nullptr, nullptr, nullptr, nullptr, nullptr, nullptr,
                nullptr, nullptr, nullptr, nullptr, nullptr, nullptr,
                nullptr, nullptr, nullptr,
                /*numHeads*/ (int64_t)n_heads_,
                /*scaleValue*/ scale,
                /*preTokens*/ (int64_t)65535, /*nextTokens*/ (int64_t)65535,
                layout,
                /*numKeyValueHeads*/ (int64_t)n_kv_,
                /*sparseMode*/ 0, /*innerPrecise*/ 0,
                /*blockSize*/ 0, /*antiquantMode*/ 0,
                /*softmaxLseFlag*/ false,
                /*keyAntiquantMode*/ 0, /*valueAntiquantMode*/ 0,
                t_attn_out_bsnd, nullptr, &fa_ws, &fa_exec));
            ensure_workspace((size_t)fa_ws);
            void *ws = fa_ws > 0 ? workspace_dev_ : nullptr;
            ACL_CHECK_RET(g_cann.aclnnFusedInferAttentionScoreV2(
                ws, fa_ws, fa_exec, stream_));
        }

        g_cann.aclDestroyTensorList(t_k_list);
        g_cann.aclDestroyTensorList(t_v_list);
        g_cann.aclDestroyTensor(t_q_bsnd);
        g_cann.aclDestroyTensor(t_attn_out_bsnd);

        // O-proj (W8). Phase A.1: under inplace AddRmsNorm, we redirect the
        // O-proj output directly into normed_dev_ so that InplaceAddRmsNorm's
        // x1Ref (= normed_dev_) already contains the addend it needs. The
        // previous consumers of normed_dev_ (Q/K/V projections) completed at
        // QKV time above, so overwriting here is safe.
        //
        // Phase A.2 (V2 RoPE): O-proj reads from wherever FIAv2 wrote its
        // output — q_dev_ in the v1 path, attn_out_dev_ in the V2 path (see
        // `fia_out_dst_dev` swap above).
        void *post_attn_rhs_dev = cp_inplace_ars_applied_ ? normed_dev_
                                                          : o_out_dev_;
        void *attn_out_src_dev = cp_rope_v2_applied_ ? attn_out_dev_ : q_dev_;
        {
            aclTensor *t_q_row = tensor_2d(attn_out_src_dev, 1, q_dim_,
                                            ACL_FLOAT16);
            aclTensor *t_o_row = tensor_2d(post_attn_rhs_dev, 1, cp_hidden_,
                                            ACL_FLOAT16);
            w8_matmul_(t_q_row, layer_w_[il].o_proj_w_i8,
                        layer_w_[il].o_proj_scale, cp_hidden_, q_dim_, t_o_row);
            g_cann.aclDestroyTensor(t_q_row);
            g_cann.aclDestroyTensor(t_o_row);
        }

        if (cp_inplace_ars_applied_) {
            // Phase A.1 inplace variant:
            //   x1Ref = normed_dev_ (holds O-proj output; overwritten with
            //           rmsnorm(o_out + residual)).
            //   x2Ref = residual_dev_ (holds prior residual; overwritten with
            //           o_out + residual — the new running residual).
            // No subsequent residual-copy needed: residual_dev_ is already
            // the residual accumulator on exit.
            aclTensor *t_x1 =
                tensor_2d(normed_dev_,   1, cp_hidden_, ACL_FLOAT16);
            aclTensor *t_x2 =
                tensor_2d(residual_dev_, 1, cp_hidden_, ACL_FLOAT16);
            uint64_t ws_needed = 0; aclOpExecutor *exec = nullptr;
            ACL_CHECK_RET(g_cann.aclnnInplaceAddRmsNormGetWorkspaceSize(
                t_x1, t_x2, lt.post_ln_f16, (double)eps_,
                t_.rstd_11,
                &ws_needed, &exec));
            ensure_workspace((size_t)ws_needed);
            void *ws = ws_needed > 0 ? workspace_dev_ : nullptr;
            ACL_CHECK_RET(g_cann.aclnnInplaceAddRmsNorm(ws, ws_needed, exec,
                                                          stream_));
            g_cann.aclDestroyTensor(t_x1);
            g_cann.aclDestroyTensor(t_x2);
        } else {
            // Fused post-attn Add+RmsNorm: xOut=cur_dev_, yOut=normed_dev_.
            aclTensor *t_resid_row =
                tensor_2d(residual_dev_, 1, cp_hidden_, ACL_FLOAT16);
            aclTensor *t_o_out_row =
                tensor_2d(o_out_dev_,    1, cp_hidden_, ACL_FLOAT16);
            uint64_t ws_needed = 0; aclOpExecutor *exec = nullptr;
            ACL_CHECK_RET(g_cann.aclnnAddRmsNormGetWorkspaceSize(
                t_resid_row, t_o_out_row, lt.post_ln_f16, (double)eps_,
                t_.normed_row, t_.rstd_11, t_.cur_row,
                &ws_needed, &exec));
            ensure_workspace((size_t)ws_needed);
            void *ws = ws_needed > 0 ? workspace_dev_ : nullptr;
            ACL_CHECK_RET(g_cann.aclnnAddRmsNorm(ws, ws_needed, exec,
                                                   stream_));
            g_cann.aclDestroyTensor(t_resid_row);
            g_cann.aclDestroyTensor(t_o_out_row);
            // residual = cur (aclnn copy, not D2D memcpy).
            aclnn_copy_cp_(residual_dev_, cur_dev_);
        }

        // FFN gate/up/silu/mul/down (W8). Phase A.1: the input to gate/up
        // projections comes from whichever buffer holds the post-attn RmsNorm
        // output. Under the inplace variant that was written into normed_dev_
        // by the InplaceAddRmsNorm above; the stock fused variant also writes
        // into normed_dev_ via AddRmsNorm's yOut binding. So the source is
        // `normed_dev_` in both cases — unchanged.
        //
        // Down-proj destination: we reroute to normed_dev_ under the inplace
        // variant so that the subsequent InplaceAddRmsNorm's x1Ref already
        // carries the FFN output. Stock path keeps ffn_out_dev_ (its
        // AddRmsNorm consumes it as the x2 addend).
        void *post_ffn_rhs_dev = cp_inplace_ars_applied_ ? normed_dev_
                                                         : ffn_out_dev_;
        if (cp_ffn_v3_applied_) {
            // Phase B: fused SwiGLU FFN — one op replaces gate-Mm+up-Mm+
            // SiLU+mul+down-Mm. Intermediate gate/up buffers unused.
            aclTensor *t_n_row   = tensor_2d(normed_dev_, 1, cp_hidden_, ACL_FLOAT16);
            aclTensor *t_ffn_row = tensor_2d(post_ffn_rhs_dev, 1, cp_hidden_,
                                              ACL_FLOAT16);
            ffn_v3_swiglu_(t_n_row,
                           layer_w_[il].gate_up_w_i8,
                           layer_w_[il].gate_up_scale,
                           layer_w_[il].down_proj_w_i8,
                           layer_w_[il].down_proj_scale,
                           t_ffn_row);
            g_cann.aclDestroyTensor(t_n_row);
            g_cann.aclDestroyTensor(t_ffn_row);
        } else {
            aclTensor *t_n_row    = tensor_2d(normed_dev_, 1, cp_hidden_, ACL_FLOAT16);
            aclTensor *t_gate_row = tensor_2d(gate_dev_, 1, inter_, ACL_FLOAT16);
            aclTensor *t_up_row   = tensor_2d(up_dev_,   1, inter_, ACL_FLOAT16);
            aclTensor *t_ffn_row  = tensor_2d(post_ffn_rhs_dev, 1, cp_hidden_,
                                               ACL_FLOAT16);
            w8_matmul_(t_n_row, layer_w_[il].gate_proj_w_i8,
                        layer_w_[il].gate_proj_scale, inter_, cp_hidden_,
                        t_gate_row);
            w8_matmul_(t_n_row, layer_w_[il].up_proj_w_i8,
                        layer_w_[il].up_proj_scale,   inter_, cp_hidden_,
                        t_up_row);
            CANN_OP(Silu, t_.gate_flat, t_.gate_flat);
            CANN_OP(InplaceMul, t_.gate_flat, t_.up_flat);
            w8_matmul_(t_gate_row, layer_w_[il].down_proj_w_i8,
                        layer_w_[il].down_proj_scale, cp_hidden_, inter_,
                        t_ffn_row);
            g_cann.aclDestroyTensor(t_n_row);
            g_cann.aclDestroyTensor(t_gate_row);
            g_cann.aclDestroyTensor(t_up_row);
            g_cann.aclDestroyTensor(t_ffn_row);
        }

        const bool is_last = (il + 1 == layers_to_run);
        const aclTensor *next_gamma =
            is_last ? t_.final_norm_f16 : t_.layer[il + 1].input_ln_f16;
        if (cp_inplace_ars_applied_) {
            // Post-FFN inplace variant:
            //   x1Ref = normed_dev_ (holds down_proj output; overwritten
            //           with rmsnorm(ffn_out + residual, next_gamma)).
            //   x2Ref = residual_dev_ (overwritten with new residual for
            //           the next layer's post-attn Add).
            // After the last layer, next_gamma == final_norm so normed_dev_
            // ends up holding rmsnorm(h, final_norm) — i.e. the view already
            // used by t_.output_f16 for the F32 cast below.
            aclTensor *t_x1 =
                tensor_2d(normed_dev_,   1, cp_hidden_, ACL_FLOAT16);
            aclTensor *t_x2 =
                tensor_2d(residual_dev_, 1, cp_hidden_, ACL_FLOAT16);
            uint64_t ws_needed = 0; aclOpExecutor *exec = nullptr;
            ACL_CHECK_RET(g_cann.aclnnInplaceAddRmsNormGetWorkspaceSize(
                t_x1, t_x2, next_gamma, (double)eps_,
                t_.rstd_11,
                &ws_needed, &exec));
            ensure_workspace((size_t)ws_needed);
            void *ws = ws_needed > 0 ? workspace_dev_ : nullptr;
            ACL_CHECK_RET(g_cann.aclnnInplaceAddRmsNorm(ws, ws_needed, exec,
                                                          stream_));
            g_cann.aclDestroyTensor(t_x1);
            g_cann.aclDestroyTensor(t_x2);
        } else {
            // Fused post-FFN Add+RmsNorm with next layer's gamma (or
            // final_norm).
            aclTensor *t_resid_row =
                tensor_2d(residual_dev_, 1, cp_hidden_, ACL_FLOAT16);
            aclTensor *t_ffn_row =
                tensor_2d(ffn_out_dev_,  1, cp_hidden_, ACL_FLOAT16);
            uint64_t ws_needed = 0; aclOpExecutor *exec = nullptr;
            ACL_CHECK_RET(g_cann.aclnnAddRmsNormGetWorkspaceSize(
                t_resid_row, t_ffn_row, next_gamma, (double)eps_,
                t_.normed_row, t_.rstd_11, t_.cur_row,
                &ws_needed, &exec));
            ensure_workspace((size_t)ws_needed);
            void *ws = ws_needed > 0 ? workspace_dev_ : nullptr;
            ACL_CHECK_RET(g_cann.aclnnAddRmsNorm(ws, ws_needed, exec,
                                                   stream_));
            g_cann.aclDestroyTensor(t_resid_row);
            g_cann.aclDestroyTensor(t_ffn_row);
        }
    }

    // With fusion, final RmsNorm was already folded into the last layer's
    // post-FFN AddRmsNorm (gamma=final_norm). So just cast to F32.
    CANN_OP(Cast, t_.output_f16, ACL_FLOAT, t_.output_f32);

    g_cann.aclDestroyTensor(t_cos);
    g_cann.aclDestroyTensor(t_sin);
}

// ============================================================================
// G2: capture the 17 per-pos forward graphs at engine init. No-op unless
// env gate set + canonical path. Failures are reported but non-fatal; we flip
// aclgraph_applied_ = false so runtime drops to eager.
// ============================================================================

void CpCannEngine::capture_aclgraph_forwards_() {
    if (!aclgraph_enabled_) return;
    if (!g_cann.has_aclgraph()) {
        fprintf(stderr, "[cp_cann] TALKER_CP_ACLGRAPH=1 but runtime lacks "
                        "aclmdlRI* — falling back to eager.\n");
        return;
    }
    if (!(cp_fusion_applied_ && w8_applied_ && !cp_ascendc_applied_)) {
        fprintf(stderr, "[cp_cann] TALKER_CP_ACLGRAPH=1 but build config is "
                        "not the canonical W8+fusion path — aclgraph disabled.\n");
        return;
    }
    // Phase A.1.4: disable aclGraph capture while the inplace AddRmsNorm
    // variant is active. The inplace op aliases x1Ref and x2Ref with their
    // output slots, which may conflict with the captured tensor-descriptor
    // assumptions in CANN 8.3.RC1. Phase A.3 will re-enable once parity is
    // verified. Callers that opt into inplace but leave TALKER_CP_ACLGRAPH=1
    // set see the eager inplace path (still a win over pure stock).
    if (cp_inplace_ars_applied_) {
        fprintf(stderr, "[cp_cann] TALKER_CP_ACLGRAPH=1 but "
                        "TALKER_CP_INPLACE_ADDRMSNORM is also set — "
                        "aclgraph capture disabled for A.1/A.2 (re-enabled "
                        "in A.3 after parity verified).\n");
        return;
    }
    // Phase B: disable aclGraph capture while FFN_V3 is on. The FFN op-count
    // changed (5→1) so the captured pos-keyed graphs would need to re-capture
    // with FFNV3 in place. Conservative choice: stay on eager FFNV3 for
    // first landing; a follow-up (B.A.3-style) can re-enable after verifying
    // the capture sees the fused op correctly.
    if (cp_ffn_v3_applied_) {
        fprintf(stderr, "[cp_cann] TALKER_CP_ACLGRAPH=1 but "
                        "TALKER_CP_FFN_V3 is also set — aclgraph capture "
                        "disabled for Phase B landing (re-enabled after "
                        "FFNV3-captured parity verified).\n");
        return;
    }
    // Phase A.2: disable aclGraph capture while V2 RoPE is on. V2 writes Q
    // AND K in-place; captured tensor descriptors encode the original
    // read-from-q_dev_ / write-to-attn_out_dev_ binding, which would mis-fire
    // under V2's write-back-to-q_dev_ pattern. Mirror of the A.1 inplace
    // AddRmsNorm gate above — the debug doc (v2_rope_numerics_debug.md:152)
    // notes this was correct in the original A.2 patch, and keeping it off
    // isolates V2 as a pure op-fusion change without aclGraph-interaction
    // risk. Re-enable once descriptor-parity is verified under capture.
    if (cp_rope_v2_applied_) {
        fprintf(stderr, "[cp_cann] TALKER_CP_ACLGRAPH=1 but "
                        "TALKER_CP_ROPE_V2 is also set — aclgraph capture "
                        "disabled for Phase A.2 re-wire (in-place V2 RoPE "
                        "conflicts with captured descriptor binding).\n");
        return;
    }
    // Phase GMM-wire: disable aclGraph capture while GMM is on. GMM replaces
    // 3 separate WQBMMv3 calls (op-count change, same as FFN_V3 above) and
    // its outputs differ by 1-2 F16 ulp from the reference, so any previously
    // captured graphs would drift out of the byte-identical window that G2/G3
    // parity gates lock in. Conservative: eager-only for first landing; a
    // follow-up can re-capture with GMM in place once drift is characterised
    // under replay.
    if (cp_gmm_qkv_applied_) {
        fprintf(stderr, "[cp_cann] TALKER_CP_ACLGRAPH=1 but "
                        "TALKER_CP_GMM_QKV is also set — aclgraph capture "
                        "disabled for Phase GMM-wire landing (op-count "
                        "change + 1.7e-3 relative drift vs 3-call reference "
                        "conflicts with byte-identical parity gates).\n");
        return;
    }

    // Allocate the zero-buffer used by aclnn_copy_cp_ (aclnnAdd with
    // `self + 1.0 * zero = self` pattern).
    if (!zero_f16_cp_dev_) {
        alloc_dev(&zero_f16_cp_dev_, (size_t)cp_hidden_ * sizeof(uint16_t));
        std::vector<uint16_t> zeros(cp_hidden_, 0);
        ACL_CHECK_RET(g_cann.aclrtMemcpy(
            zero_f16_cp_dev_, cp_hidden_ * sizeof(uint16_t),
            zeros.data(), cp_hidden_ * sizeof(uint16_t),
            ACL_MEMCPY_HOST_TO_DEVICE));
    }

    aclgraph_graphs_.assign(MAX_ACLGRAPH_POS + 1, nullptr);

    // Stage a deterministic non-zero input into input_stage_f32_dev_ so the
    // capture-time forward has plausible numerics (no denormals, no NaN).
    {
        std::vector<float> stub(talker_hidden_, 0.01f);
        ACL_CHECK_RET(g_cann.aclrtMemcpy(
            input_stage_f32_dev_, talker_hidden_ * sizeof(float),
            stub.data(), talker_hidden_ * sizeof(float),
            ACL_MEMCPY_HOST_TO_DEVICE));
    }

    int captured = 0, failed = 0;
    aclmdlRICaptureMode mode = ACL_MODEL_RI_CAPTURE_MODE_GLOBAL;
    for (int p = 0; p <= MAX_ACLGRAPH_POS; ++p) {
        // Drain the stream before opening a new capture — prevents cross-
        // capture state pollution in the driver's task-DAG tracker.
        ACL_CHECK_RET(g_cann.aclrtSynchronizeStream(stream_));

        aclError cb = g_cann.aclmdlRICaptureBegin(stream_, mode);
        if (cb != 0 && p == 0 && mode == ACL_MODEL_RI_CAPTURE_MODE_GLOBAL) {
            // Try RELAXED as fallback (stop-rule #1).
            fprintf(stderr, "[cp_cann] CaptureBegin GLOBAL rejected (err=%d); "
                            "retrying with RELAXED...\n", (int)cb);
            mode = ACL_MODEL_RI_CAPTURE_MODE_RELAXED;
            cb = g_cann.aclmdlRICaptureBegin(stream_, mode);
        }
        if (cb != 0) {
            fprintf(stderr, "[cp_cann] CaptureBegin pos=%d err=%d: %s — "
                            "aborting aclgraph init, falling back to eager.\n",
                    p, (int)cb,
                    g_cann.aclGetRecentErrMsg ? g_cann.aclGetRecentErrMsg() : "?");
            // Cleanup any partial handles and disable.
            for (auto &g : aclgraph_graphs_) {
                if (g) { g_cann.aclmdlRIDestroy(g); g = nullptr; }
            }
            aclgraph_graphs_.clear();
            return;
        }

        // Dispatch the capturable forward for position p.
        forward_one_token_capturable_(p);

        aclmdlRI ri = nullptr;
        aclError ce = g_cann.aclmdlRICaptureEnd(stream_, &ri);
        if (ce != 0 || ri == nullptr) {
            fprintf(stderr, "[cp_cann] CaptureEnd pos=%d err=%d, ri=%p — "
                            "aborting aclgraph init.\n", p, (int)ce, (void *)ri);
            if (ri) g_cann.aclmdlRIDestroy(ri);
            for (auto &g : aclgraph_graphs_) {
                if (g) { g_cann.aclmdlRIDestroy(g); g = nullptr; }
            }
            aclgraph_graphs_.clear();
            return;
        }

        aclgraph_graphs_[p] = ri;
        ++captured;
    }

    printf("[cp_cann] G2 aclGraph capture: %d/%d positions succeeded "
           "(mode=%s)\n", captured, MAX_ACLGRAPH_POS + 1,
           mode == ACL_MODEL_RI_CAPTURE_MODE_GLOBAL ? "GLOBAL" : "RELAXED");
    if (captured == MAX_ACLGRAPH_POS + 1) {
        aclgraph_applied_ = true;
    }
    (void)failed;
}

// ============================================================================
// forward_one_token — F16 end-to-end with F32 staging at the boundaries.
// ============================================================================

// M6.2: launch path queues every op for position `pos` on `stream_` and
// returns without syncing or D2H. Records `forward_done_event_` so callers
// can fence from another stream (Talker[N+1] waiting on CP[N] final group).
//
// M3'new' additions (default-off, existing callers see identical behavior):
//   - use_async_memcpy: when true, use `aclrtMemcpyAsync` on `stream_` for
//     the H2D input upload. This makes the upload stream-ordered (not
//     host-synchronous). Used by `forward_two_tokens_launch` so pos 1's
//     input upload serializes on stream_ after pos 0's compute has read
//     `input_stage_f32_dev_`, without host blocking between the two calls.
//   - record_event: when false, skip recording `forward_done_event_` at
//     the tail (both aclGraph fast path and eager path). Used by
//     `forward_two_tokens_launch` for the pos-0 call; the pos-1 call then
//     records the event for the single aggregated fence.
void CpCannEngine::forward_one_token_launch(const float *input_talker_space,
                                             int pos,
                                             aclrtEvent wait_event,
                                             bool use_async_memcpy,
                                             bool record_event) {
    assert(ready_);

    if (wait_event && g_cann.aclrtStreamWaitEvent) {
        ACL_CHECK_RET(g_cann.aclrtStreamWaitEvent(stream_, wait_event));
    }

    // 1. Upload F32 input embedding to f32 stage buffer.
    if (use_async_memcpy && g_cann.aclrtMemcpyAsync) {
        // Stream-ordered H2D — the device will read `input_stage_f32_dev_`
        // only after all prior queued work on `stream_` has finished
        // consuming it (the input projection Mm right below is the sole
        // reader, so subsequent pos-1 writes via this same async path are
        // safe once that Mm has drained on the stream).
        ACL_CHECK_RET(g_cann.aclrtMemcpyAsync(
            input_stage_f32_dev_, talker_hidden_ * sizeof(float),
            input_talker_space,   talker_hidden_ * sizeof(float),
            ACL_MEMCPY_HOST_TO_DEVICE, stream_));
    } else {
        ACL_CHECK_RET(g_cann.aclrtMemcpy(
            input_stage_f32_dev_, talker_hidden_ * sizeof(float),
            input_talker_space,   talker_hidden_ * sizeof(float),
            ACL_MEMCPY_HOST_TO_DEVICE));
    }

    // ---------- G2: aclGraph replay fast path ----------
    // When `aclgraph_applied_` AND pos is within the captured cache (0..MAX),
    // swap the full eager dispatch for a single `aclmdlRIExecuteAsync` call on
    // `stream_`. The captured graph bakes every pos-dependent pointer (RoPE
    // slice, V-cache slot, FIAv2 seq_len), so replay is just one driver call.
    // Fall through to the eager path for unknown pos or when the capture was
    // not done (env unset, or non-canonical build config).
    if (aclgraph_applied_ && pos >= 0 && pos <= MAX_ACLGRAPH_POS &&
        (size_t)pos < aclgraph_graphs_.size() && aclgraph_graphs_[pos] != nullptr) {
        ACL_CHECK_RET(g_cann.aclmdlRIExecuteAsync(aclgraph_graphs_[pos],
                                                    stream_));
        if (record_event && forward_done_event_ && g_cann.aclrtRecordEvent) {
            ACL_CHECK_RET(g_cann.aclrtRecordEvent(forward_done_event_,
                                                    stream_));
        }
        return;
    }
    // ----------------------------------------------------

    // 2. Input projection in F32 (proj_w/proj_b are F32; input is F32).
    //    Output goes to proj_out_f32_dev_ (F32). This matches the precision
    //    llama.cpp uses — cp_matvec_f32 on CPU — which is the only reference
    //    that produces audibly-correct output.
    {
        aclTensor *t_x = tensor_2d(input_stage_f32_dev_,
                                    talker_hidden_, 1, ACL_FLOAT);
        CANN_OP(Mm, t_.proj_w_f32, t_x, t_.proj_out_f32_col,
                /*cubeMathType=*/0);
        g_cann.aclDestroyTensor(t_x);
    }
    // Add proj_b in F32: F32 Add with alpha=1.
    {
        float one = 1.0f;
        aclScalar *alpha_f32 = g_cann.aclCreateScalar(&one, ACL_FLOAT);
        CANN_OP(InplaceAdd, t_.proj_out_f32_flat, t_.proj_b_f32, alpha_f32);
        g_cann.aclDestroyScalar(alpha_f32);
    }
    // Cast F32 projected -> F16 cur_dev_ so the first RmsNorm can run F16
    // with F32 gamma (matches ggml-cann's Qwen3 precision convention).
    // From this point on the transformer runs F16 end-to-end, residual adds
    // included — this is the llama.cpp/ggml-cann convention.
    CANN_OP(Cast, t_.proj_out_f32_flat, ACL_FLOAT16,
            t_.cur_f16_flat_as_target);

    const int seq_len = pos + 1;
    const int group   = n_heads_ / n_kv_;

    uint16_t *cos_pos = (uint16_t *)rope_cos_dev_ + (size_t)pos * head_dim_;
    uint16_t *sin_pos = (uint16_t *)rope_sin_dev_ + (size_t)pos * head_dim_;
    aclTensor *t_cos = [&]() {
        int64_t shape[4]   = {1, 1, 1, (int64_t)head_dim_};
        int64_t strides[4] = {(int64_t)head_dim_, (int64_t)head_dim_,
                               (int64_t)head_dim_, 1};
        return tensor_strided(cos_pos, 4, shape, strides);
    }();
    aclTensor *t_sin = [&]() {
        int64_t shape[4]   = {1, 1, 1, (int64_t)head_dim_};
        int64_t strides[4] = {(int64_t)head_dim_, (int64_t)head_dim_,
                               (int64_t)head_dim_, 1};
        return tensor_strided(sin_pos, 4, shape, strides);
    }();

    // Hidden state lives in cur_dev_ (F16) throughout the transformer.
    // Residual saved via F16 d2d memcpy at the start of each sublayer, then
    // added back with aclnnAdd (F16) at the end. This is the ggml-cann/
    // llama.cpp convention — the model was trained with F16 residuals.
    //
    // W3b CP kernel fusion (cp_fusion_applied_):
    //   Layer 0 starts unfused: `residual=cur` d2d + `RmsNorm(cur, input_ln[0])
    //   → normed`. Layer 1+ skips both — the PREVIOUS layer's post-FFN
    //   AddRmsNorm already left `normed_dev_` holding rmsnorm(cur, input_ln[il])
    //   and `cur_dev_` holding the updated residual sum. The per-sublayer
    //   post-Add+pre-next-norm pair collapses into one aclnnAddRmsNorm call.
    const int layers_to_run = (active_layers_ > 0 && active_layers_ < n_layers_)
                               ? active_layers_ : n_layers_;
    for (int il = 0; il < layers_to_run; ++il) {
        const auto &lt = t_.layer[il];

        // Layer-start residual save + input RmsNorm. With fusion, this is
        // only needed for layer 0 (the entry point); subsequent layers have
        // `normed_dev_` pre-populated by the prior layer's post-FFN
        // AddRmsNorm.
        //
        // Phase A.1 (inplace AddRmsNorm): residual_dev_ is maintained in
        // place across layers by the InplaceAddRmsNorm's x2Ref output. The
        // per-layer `residual=cur` copy is skipped. Layer 0 still seeds
        // residual_dev_ from cur_dev_ because input projection wrote the
        // first hidden into cur_dev_.
        if (!cp_fusion_applied_ || il == 0) {
            // residual = cur (F16 d2d async memcpy)
            ACL_CHECK_RET(g_cann.aclrtMemcpyAsync(
                residual_dev_, cp_hidden_ * sizeof(uint16_t),
                cur_dev_,      cp_hidden_ * sizeof(uint16_t),
                ACL_MEMCPY_DEVICE_TO_DEVICE, stream_));

            CANN_OP(RmsNorm, t_.cur_row, lt.input_ln, (double)eps_,
                    t_.normed_row, t_.rstd_11);
        } else if (!cp_inplace_ars_applied_) {
            // Fused path for il > 0: the prior layer's post-FFN AddRmsNorm
            // left `cur_dev_ = prev_residual + prev_ffn_out` and
            // `normed_dev_ = RmsNorm(cur, input_ln[il])`. We still need
            // `residual = cur` for THIS layer's post-attn Add.
            ACL_CHECK_RET(g_cann.aclrtMemcpyAsync(
                residual_dev_, cp_hidden_ * sizeof(uint16_t),
                cur_dev_,      cp_hidden_ * sizeof(uint16_t),
                ACL_MEMCPY_DEVICE_TO_DEVICE, stream_));
        }
        // else (inplace fused path, il > 0): residual_dev_ already holds the
        // running residual from the previous layer's InplaceAddRmsNorm.

        // ----------------------------------------------------------------
        // Path C W4.1: fused attention sublayer via AscendC custom kernel.
        // Collapses the 12-op aclnn chain (RmsNorm → Q/K/V W8-Mm → QK-norm
        // → RoPE → V-copy → FusedInferAttention → O-Mm → +residual) into
        // one dispatch of `aclrtlaunch_fused_attn_sublayer`.
        //
        // Preconditions at entry (satisfied by the layer-start block
        // above):
        //   residual_dev_ = current hidden (F16 [cp_hidden])
        //   normed_dev_   = RmsNorm(residual, input_ln[il])   (F16)
        //     (either pre-computed by the previous layer's post-FFN
        //     AddRmsNorm fusion, or by this layer's explicit RmsNorm.)
        //
        // Kernel postconditions:
        //   residual_dev_ = old_residual + O           (the new `cur`)
        //   k_cache[il][pos, :] / v_cache[il][pos, :]  populated
        //   o_out_dev_    = O (staging, for back-compat with callers that
        //                  read it; not reused by the post-attn path)
        //
        // Gated on cp_ascendc_applied_ (build-time QWEN_TTS_HAS_ASCENDC +
        // runtime TALKER_CP_ASCENDC=1 + w8_applied_). Unset = stock chain.
        // ----------------------------------------------------------------
#ifdef QWEN_TTS_HAS_ASCENDC
        if (cp_ascendc_applied_) {
            const uint32_t opts = 1u;  // bit 0: normed pre-computed in normed_dev_
            uint32_t eps_bits;
            { float e = eps_; std::memcpy(&eps_bits, &e, 4); }
            uint32_t rc = aclrtlaunch_fused_attn_sublayer(
                /*blockDim=*/1, stream_,
                residual_dev_, normed_dev_, layer_w_[il].input_ln_w_f16,
                layer_w_[il].q_proj_w_i8, layer_w_[il].q_proj_scale,
                layer_w_[il].k_proj_w_i8, layer_w_[il].k_proj_scale,
                layer_w_[il].v_proj_w_i8, layer_w_[il].v_proj_scale,
                layer_w_[il].o_proj_w_i8, layer_w_[il].o_proj_scale,
                layer_w_[il].q_norm_w_f16, layer_w_[il].k_norm_w_f16,
                cos_pos, sin_pos,
                // The kernel expects the FULL [MAX_SEQ, kv_dim] cache base
                // (it indexes the seq_len-1 slot itself — see AppendKVCache_).
                k_cache_dev_[il], v_cache_dev_[il],
                o_out_dev_,
                /*scratch_q=*/q_dev_, /*scratch_scores=*/scores_f16_dev_,
                /*seq_len=*/(uint32_t)(pos + 1), eps_bits, opts);
            if (rc != 0) {
                fprintf(stderr,
                        "[cp_cann] aclrtlaunch_fused_attn_sublayer rc=%u "
                        "(layer %d, pos %d)\n", rc, il, pos);
            }

            // Post-kernel: residual_dev_ holds (old_residual + O) — the new
            // cur. Copy into cur_dev_ and RmsNorm into normed_dev_ so the
            // FFN matmuls see the same buffer layout they do in the stock
            // path. residual_dev_ is left pointing at `cur` for the FFN
            // sublayer's own Add (identical value; no re-memcpy needed).
            ACL_CHECK_RET(g_cann.aclrtMemcpyAsync(
                cur_dev_,      cp_hidden_ * sizeof(uint16_t),
                residual_dev_, cp_hidden_ * sizeof(uint16_t),
                ACL_MEMCPY_DEVICE_TO_DEVICE, stream_));
            CANN_OP(RmsNorm, t_.cur_row, lt.post_ln, (double)eps_,
                    t_.normed_row, t_.rstd_11);

            // Skip the stock attn chain AND the post-attn Add+RmsNorm
            // block — the kernel + this post-amble fully replace them.
            goto ascendc_attn_done;
        }
#endif

        // The stock attn chain + post-attn Add+RmsNorm live in this block.
        // Wrapped so the goto above (ASCENDC branch) can jump past all the
        // declarations inside without tripping "jump crosses initialization".
        {
        // Three-way split: W8 (Stretch S1) > NZ (M5.3) > ND (default).
        if (w8_applied_) {
            // GMM-wire variant: one aclnnGroupedMatmulV3 dispatch replaces
            // the three w8_matmul_ calls. Outputs go to the canonical
            // q_dev_ / k_dev_ / v_dev_ buffers (same destinations the
            // stock path writes to).
            if (cp_gmm_qkv_applied_) {
                gmm_qkv_(normed_dev_, il, q_dev_, k_dev_, v_dev_);
            } else {
                aclTensor *t_n_row = tensor_2d(normed_dev_, 1, cp_hidden_, ACL_FLOAT16);
                aclTensor *t_q_row = tensor_2d(q_dev_, 1, q_dim_, ACL_FLOAT16);
                aclTensor *t_k_row = tensor_2d(k_dev_, 1, kv_dim_, ACL_FLOAT16);
                aclTensor *t_v_row = tensor_2d(v_dev_, 1, kv_dim_, ACL_FLOAT16);
                w8_matmul_(t_n_row, layer_w_[il].q_proj_w_i8,
                            layer_w_[il].q_proj_scale, q_dim_, cp_hidden_, t_q_row);
                w8_matmul_(t_n_row, layer_w_[il].k_proj_w_i8,
                            layer_w_[il].k_proj_scale, kv_dim_, cp_hidden_, t_k_row);
                w8_matmul_(t_n_row, layer_w_[il].v_proj_w_i8,
                            layer_w_[il].v_proj_scale, kv_dim_, cp_hidden_, t_v_row);
                g_cann.aclDestroyTensor(t_n_row);
                g_cann.aclDestroyTensor(t_q_row);
                g_cann.aclDestroyTensor(t_k_row);
                g_cann.aclDestroyTensor(t_v_row);
            }
        } else if (nz_applied_) {
            auto wT_nz = [&](void *buf, int64_t rows, int64_t cols) {
                int64_t shape[2]   = {cols, rows};
                int64_t strides[2] = {1, cols};
                return make_tensor(buf, 2, shape, strides, ACL_FLOAT16,
                                    ACL_FORMAT_FRACTAL_NZ);
            };
            aclTensor *t_wq_T = wT_nz(layer_w_[il].q_proj_w, q_dim_,  cp_hidden_);
            aclTensor *t_wk_T = wT_nz(layer_w_[il].k_proj_w, kv_dim_, cp_hidden_);
            aclTensor *t_wv_T = wT_nz(layer_w_[il].v_proj_w, kv_dim_, cp_hidden_);
            aclTensor *t_n_row = tensor_2d(normed_dev_, 1, cp_hidden_);
            aclTensor *t_q_row = tensor_2d(q_dev_, 1, q_dim_);
            aclTensor *t_k_row = tensor_2d(k_dev_, 1, kv_dim_);
            aclTensor *t_v_row = tensor_2d(v_dev_, 1, kv_dim_);
            CANN_OP(Mm, t_n_row, t_wq_T, t_q_row, (int8_t)0);
            CANN_OP(Mm, t_n_row, t_wk_T, t_k_row, (int8_t)0);
            CANN_OP(Mm, t_n_row, t_wv_T, t_v_row, (int8_t)0);
            g_cann.aclDestroyTensor(t_wq_T);
            g_cann.aclDestroyTensor(t_wk_T);
            g_cann.aclDestroyTensor(t_wv_T);
            g_cann.aclDestroyTensor(t_n_row);
            g_cann.aclDestroyTensor(t_q_row);
            g_cann.aclDestroyTensor(t_k_row);
            g_cann.aclDestroyTensor(t_v_row);
        } else {
            CANN_OP(Mm, lt.q_proj, t_.normed_col, t_.q_col, /*cubeMathType=*/0);
            CANN_OP(Mm, lt.k_proj, t_.normed_col, t_.k_col, /*cubeMathType=*/0);
            CANN_OP(Mm, lt.v_proj, t_.normed_col, t_.v_col, /*cubeMathType=*/0);
        }

        CANN_OP(RmsNorm, t_.q_heads, lt.q_norm, (double)eps_,
                t_.q_heads, t_.rstd_heads);
        CANN_OP(RmsNorm, t_.k_kv,    lt.k_norm, (double)eps_,
                t_.k_kv,    t_.rstd_kv);

        // RoPE: v1 two-call NEOX or V2 fused in-place (Phase A.2). See the
        // capturable mirror above for hypothesis-fix rationale.
        uint16_t *k_cache_slot =
            (uint16_t *)k_cache_dev_[il] + (size_t)pos * kv_dim_;
        if (cp_rope_v2_applied_) {
            aclTensor *t_q_rope_v2 = [&]() {
                int64_t shape[4]   = {1, 1, (int64_t)n_heads_, (int64_t)head_dim_};
                int64_t strides[4] = {(int64_t)q_dim_, (int64_t)q_dim_,
                                       (int64_t)head_dim_, 1};
                return tensor_strided(q_dev_, 4, shape, strides);
            }();
            aclTensor *t_k_rope_v2 = [&]() {
                int64_t shape[4]   = {1, 1, (int64_t)n_kv_, (int64_t)head_dim_};
                int64_t strides[4] = {(int64_t)kv_dim_, (int64_t)kv_dim_,
                                       (int64_t)head_dim_, 1};
                return tensor_strided(k_cache_slot, 4, shape, strides);
            }();
            uint64_t v2_ws = 0;
            aclOpExecutor *v2_exec = nullptr;
            char rotary_mode[5] = {'h','a','l','f',0};
            ACL_CHECK_RET(g_cann.aclnnApplyRotaryPosEmbV2GetWorkspaceSize(
                t_q_rope_v2, t_k_rope_v2, t_cos, t_sin,
                /*layout=*/(int64_t)1, rotary_mode, &v2_ws, &v2_exec));
            ensure_workspace((size_t)v2_ws);
            void *v2_ws_ptr = v2_ws > 0 ? workspace_dev_ : nullptr;
            ACL_CHECK_RET(g_cann.aclnnApplyRotaryPosEmbV2(
                v2_ws_ptr, v2_ws, v2_exec, stream_));
            g_cann.aclDestroyTensor(t_q_rope_v2);
            g_cann.aclDestroyTensor(t_k_rope_v2);
        } else {
            // RoPE (F16) — v1 NEOX two-call path.
            CANN_OP(RotaryPositionEmbedding,
                    t_.q_rope_4d, t_cos, t_sin,
                    /*mode=*/(int64_t)0, t_.attn_out_4d);

            aclTensor *t_k_rope_src = [&]() {
                int64_t shape[4]   = {1, 1, (int64_t)n_kv_, (int64_t)head_dim_};
                int64_t strides[4] = {(int64_t)kv_dim_, (int64_t)kv_dim_,
                                       (int64_t)head_dim_, 1};
                return tensor_strided(k_dev_, 4, shape, strides);
            }();
            aclTensor *t_k_rope_dst = [&]() {
                int64_t shape[4]   = {1, 1, (int64_t)n_kv_, (int64_t)head_dim_};
                int64_t strides[4] = {(int64_t)kv_dim_, (int64_t)kv_dim_,
                                       (int64_t)head_dim_, 1};
                return tensor_strided(k_cache_slot, 4, shape, strides);
            }();
            CANN_OP(RotaryPositionEmbedding,
                    t_k_rope_src, t_cos, t_sin,
                    /*mode=*/(int64_t)0, t_k_rope_dst);
            g_cann.aclDestroyTensor(t_k_rope_src);
            g_cann.aclDestroyTensor(t_k_rope_dst);
        }

        // V -> cache slot
        uint16_t *v_cache_slot =
            (uint16_t *)v_cache_dev_[il] + (size_t)pos * kv_dim_;
        ACL_CHECK_RET(g_cann.aclrtMemcpyAsync(
            v_cache_slot, kv_dim_ * sizeof(uint16_t),
            v_dev_,       kv_dim_ * sizeof(uint16_t),
            ACL_MEMCPY_DEVICE_TO_DEVICE, stream_));

        // --- Fused attention: aclnnFusedInferAttentionScoreV2. This is the
        // exact op llama.cpp's ggml-cann backend uses for Qwen3 attention.
        // By calling it here with the same params (layout="BSND",
        // innerPrecise=0 for S=1 decode, no mask), we inherit llama.cpp's
        // numerical behavior — same weights (F16 GGUF) + same op + same
        // params → same output. Prior manual BMM path diverged precisely
        // because it was a custom attention with subtly-different numerics.
        //
        // Input layouts (BSND with B=1):
        //   Q:   [1, 1, n_heads, head_dim]  over attn_out_dev_ (v1) or
        //        q_dev_ (V2 in-place RoPE)
        //   K:   [1, seq_len, n_kv, head_dim] view of KV cache (native)
        //   V:   same as K, over v_cache_dev_
        //   Out: [1, 1, n_heads, head_dim]  into q_dev_ (v1) or
        //        attn_out_dev_ (V2 — Q source/dst swap to avoid aliasing)
        void *fia_q_src_dev = cp_rope_v2_applied_ ? q_dev_ : attn_out_dev_;
        void *fia_out_dst_dev = cp_rope_v2_applied_ ? attn_out_dev_ : q_dev_;
        aclTensor *t_q_bsnd = [&]() {
            int64_t shape[4]   = {1, 1, (int64_t)n_heads_, (int64_t)head_dim_};
            int64_t strides[4] = {(int64_t)q_dim_, (int64_t)q_dim_,
                                   (int64_t)head_dim_, 1};
            return tensor_strided(fia_q_src_dev, 4, shape, strides,
                                   ACL_FLOAT16);
        }();
        aclTensor *t_k_bsnd = [&]() {
            int64_t shape[4]   = {1, (int64_t)seq_len, (int64_t)n_kv_,
                                   (int64_t)head_dim_};
            int64_t strides[4] = {(int64_t)seq_len * kv_dim_,
                                   (int64_t)kv_dim_,
                                   (int64_t)head_dim_, 1};
            return tensor_strided(k_cache_dev_[il], 4, shape, strides,
                                   ACL_FLOAT16);
        }();
        aclTensor *t_v_bsnd = [&]() {
            int64_t shape[4]   = {1, (int64_t)seq_len, (int64_t)n_kv_,
                                   (int64_t)head_dim_};
            int64_t strides[4] = {(int64_t)seq_len * kv_dim_,
                                   (int64_t)kv_dim_,
                                   (int64_t)head_dim_, 1};
            return tensor_strided(v_cache_dev_[il], 4, shape, strides,
                                   ACL_FLOAT16);
        }();
        aclTensor *t_attn_out_bsnd = [&]() {
            int64_t shape[4]   = {1, 1, (int64_t)n_heads_, (int64_t)head_dim_};
            int64_t strides[4] = {(int64_t)q_dim_, (int64_t)q_dim_,
                                   (int64_t)head_dim_, 1};
            return tensor_strided(fia_out_dst_dev, 4, shape, strides,
                                   ACL_FLOAT16);
        }();

        aclTensorList *t_k_list = g_cann.aclCreateTensorList(&t_k_bsnd, 1);
        aclTensorList *t_v_list = g_cann.aclCreateTensorList(&t_v_bsnd, 1);

        {
            uint64_t fa_ws = 0;
            aclOpExecutor *fa_exec = nullptr;
            char layout[5] = {'B','S','N','D',0};
            double scale = 1.0 / sqrt((double)head_dim_);
            ACL_CHECK_RET(g_cann.aclnnFusedInferAttentionScoreV2GetWorkspaceSize(
                t_q_bsnd, t_k_list, t_v_list,
                /*pseShift*/ nullptr, /*attenMask*/ nullptr,
                /*actSeqLen*/ nullptr, /*actSeqLenKv*/ nullptr,
                /*deqScale1*/ nullptr, /*quantScale1*/ nullptr,
                /*deqScale2*/ nullptr, /*quantScale2*/ nullptr,
                /*quantOffset2*/ nullptr,
                /*antiquantScale*/ nullptr, /*antiquantOffset*/ nullptr,
                /*blockTable*/ nullptr,
                /*queryPaddingSize*/ nullptr, /*kvPaddingSize*/ nullptr,
                /*keyAntiquantScale*/ nullptr, /*keyAntiquantOffset*/ nullptr,
                /*valueAntiquantScale*/ nullptr,
                /*valueAntiquantOffset*/ nullptr,
                /*keySharedPrefix*/ nullptr, /*valueSharedPrefix*/ nullptr,
                /*actualSharedPrefixLen*/ nullptr,
                /*numHeads*/ (int64_t)n_heads_,
                /*scaleValue*/ scale,
                /*preTokens*/ (int64_t)65535,
                /*nextTokens*/ (int64_t)65535,
                /*inputLayout*/ layout,
                /*numKeyValueHeads*/ (int64_t)n_kv_,
                /*sparseMode*/ (int64_t)0,
                /*innerPrecise*/ (int64_t)0,  // S=1 → high precision mode
                /*blockSize*/ (int64_t)0,
                /*antiquantMode*/ (int64_t)0,
                /*softmaxLseFlag*/ false,
                /*keyAntiquantMode*/ (int64_t)0,
                /*valueAntiquantMode*/ (int64_t)0,
                /*attentionOut*/ t_attn_out_bsnd,
                /*softmaxLse*/ nullptr,
                &fa_ws, &fa_exec));
            ensure_workspace((size_t)fa_ws);
            void *ws = fa_ws > 0 ? workspace_dev_ : nullptr;
            ACL_CHECK_RET(g_cann.aclnnFusedInferAttentionScoreV2(
                ws, fa_ws, fa_exec, stream_));
        }

        g_cann.aclDestroyTensorList(t_k_list);
        g_cann.aclDestroyTensorList(t_v_list);
        g_cann.aclDestroyTensor(t_q_bsnd);
        g_cann.aclDestroyTensor(t_attn_out_bsnd);
        // t_k_bsnd / t_v_bsnd are owned by the destroyed lists — no free here.

        // O projection: W8 > NZ > ND. Phase A.1: under inplace AddRmsNorm,
        // redirect the O-proj output into normed_dev_ so it becomes the
        // x1Ref addend for the subsequent InplaceAddRmsNorm call.
        // Phase A.2 (V2 RoPE): O-proj reads from whichever buffer holds the
        // FIAv2 output — attn_out_dev_ under V2, q_dev_ under v1.
        void *post_attn_rhs_dev = cp_inplace_ars_applied_ ? normed_dev_
                                                          : o_out_dev_;
        void *attn_out_src_dev = cp_rope_v2_applied_ ? attn_out_dev_ : q_dev_;
        if (w8_applied_) {
            aclTensor *t_q_row = tensor_2d(attn_out_src_dev, 1, q_dim_,
                                            ACL_FLOAT16);
            aclTensor *t_o_row = tensor_2d(post_attn_rhs_dev, 1, cp_hidden_,
                                            ACL_FLOAT16);
            w8_matmul_(t_q_row, layer_w_[il].o_proj_w_i8,
                        layer_w_[il].o_proj_scale, cp_hidden_, q_dim_, t_o_row);
            g_cann.aclDestroyTensor(t_q_row);
            g_cann.aclDestroyTensor(t_o_row);
        } else if (nz_applied_) {
            int64_t wT_shape[2]   = {(int64_t)q_dim_, (int64_t)cp_hidden_};
            int64_t wT_strides[2] = {1, (int64_t)q_dim_};
            aclTensor *t_wo_T = make_tensor(layer_w_[il].o_proj_w, 2, wT_shape,
                                             wT_strides, ACL_FLOAT16,
                                             ACL_FORMAT_FRACTAL_NZ);
            aclTensor *t_q_row = tensor_2d(attn_out_src_dev, 1, q_dim_);
            aclTensor *t_o_row = tensor_2d(post_attn_rhs_dev, 1, cp_hidden_);
            CANN_OP(Mm, t_q_row, t_wo_T, t_o_row, (int8_t)0);
            g_cann.aclDestroyTensor(t_wo_T);
            g_cann.aclDestroyTensor(t_q_row);
            g_cann.aclDestroyTensor(t_o_row);
        } else {
            // ND fallback. t_.q_col is a persistent tensor_2d over q_dev_;
            // under V2 we must build a matching view over attn_out_dev_
            // instead. O-proj target descriptor also respects the inplace
            // redirect when active.
            aclTensor *t_o_col = cp_inplace_ars_applied_
                ? tensor_2d(normed_dev_, cp_hidden_, 1, ACL_FLOAT16)
                : nullptr;
            if (cp_rope_v2_applied_) {
                aclTensor *t_attn_col = tensor_2d(attn_out_dev_, q_dim_, 1,
                                                   ACL_FLOAT16);
                if (t_o_col) {
                    CANN_OP(Mm, lt.o_proj, t_attn_col, t_o_col,
                            /*cubeMathType=*/0);
                } else {
                    CANN_OP(Mm, lt.o_proj, t_attn_col, t_.o_out_col,
                            /*cubeMathType=*/0);
                }
                g_cann.aclDestroyTensor(t_attn_col);
            } else {
                if (t_o_col) {
                    CANN_OP(Mm, lt.o_proj, t_.q_col, t_o_col,
                            /*cubeMathType=*/0);
                } else {
                    CANN_OP(Mm, lt.o_proj, t_.q_col, t_.o_out_col,
                            /*cubeMathType=*/0);
                }
            }
            if (t_o_col) g_cann.aclDestroyTensor(t_o_col);
        }

        if (cp_fusion_applied_ && cp_inplace_ars_applied_) {
            // Phase A.1 post-attn inplace: normed_dev_ already holds O-proj
            // output; x1Ref = normed_dev_, x2Ref = residual_dev_. After the
            // call normed_dev_ = rmsnorm(o_out+residual, post_ln) and
            // residual_dev_ = o_out+residual (the new running residual).
            aclTensor *t_x1 =
                tensor_2d(normed_dev_,   1, cp_hidden_, ACL_FLOAT16);
            aclTensor *t_x2 =
                tensor_2d(residual_dev_, 1, cp_hidden_, ACL_FLOAT16);
            uint64_t ws_needed = 0; aclOpExecutor *exec = nullptr;
            ACL_CHECK_RET(g_cann.aclnnInplaceAddRmsNormGetWorkspaceSize(
                t_x1, t_x2, lt.post_ln_f16, (double)eps_,
                t_.rstd_11,
                &ws_needed, &exec));
            ensure_workspace((size_t)ws_needed);
            void *ws = ws_needed > 0 ? workspace_dev_ : nullptr;
            ACL_CHECK_RET(g_cann.aclnnInplaceAddRmsNorm(ws, ws_needed, exec,
                                                          stream_));
            g_cann.aclDestroyTensor(t_x1);
            g_cann.aclDestroyTensor(t_x2);
        } else if (cp_fusion_applied_) {
            // W3b: fuse `cur = residual + o_out` + `RmsNorm(cur, post_ln)`
            // into one aclnnAddRmsNorm. xOut=cur_dev_ holds the sum (still
            // needed for the next `residual=cur` memcpy); yOut=normed_dev_
            // is ready for the FFN matmuls.
            //
            // Input/output tensors all use the `_row` ([1, cp_hidden]) shape
            // so the rstdOut scalar [1,1] lines up with the leading-dim
            // reduction the op expects. Using mixed 1D/2D shapes trips
            // `coreDim == 0` on CANN 8.5.
            aclTensor *t_resid_row =
                tensor_2d(residual_dev_, 1, cp_hidden_, ACL_FLOAT16);
            aclTensor *t_o_out_row =
                tensor_2d(o_out_dev_,    1, cp_hidden_, ACL_FLOAT16);
            uint64_t ws_needed = 0;
            aclOpExecutor *exec = nullptr;
            ACL_CHECK_RET(g_cann.aclnnAddRmsNormGetWorkspaceSize(
                t_resid_row, t_o_out_row, lt.post_ln_f16, (double)eps_,
                t_.normed_row, t_.rstd_11, t_.cur_row,
                &ws_needed, &exec));
            ensure_workspace((size_t)ws_needed);
            void *ws = ws_needed > 0 ? workspace_dev_ : nullptr;
            ACL_CHECK_RET(g_cann.aclnnAddRmsNorm(ws, ws_needed, exec,
                                                   stream_));
            g_cann.aclDestroyTensor(t_resid_row);
            g_cann.aclDestroyTensor(t_o_out_row);
            // residual = cur  (needed to prime the FFN residual Add)
            ACL_CHECK_RET(g_cann.aclrtMemcpyAsync(
                residual_dev_, cp_hidden_ * sizeof(uint16_t),
                cur_dev_,      cp_hidden_ * sizeof(uint16_t),
                ACL_MEMCPY_DEVICE_TO_DEVICE, stream_));
        } else {
            // cur = residual + o_out  (F16 add)
            CANN_OP(Add, t_.residual_flat, t_.o_out_flat, one_scalar_,
                    t_.cur_flat);

            // residual = cur  (for FFN)
            ACL_CHECK_RET(g_cann.aclrtMemcpyAsync(
                residual_dev_, cp_hidden_ * sizeof(uint16_t),
                cur_dev_,      cp_hidden_ * sizeof(uint16_t),
                ACL_MEMCPY_DEVICE_TO_DEVICE, stream_));

            CANN_OP(RmsNorm, t_.cur_row, lt.post_ln, (double)eps_,
                    t_.normed_row, t_.rstd_11);
        }
        }  // end of stock attn-chain block (paired with the `{` above).

#ifdef QWEN_TTS_HAS_ASCENDC
    ascendc_attn_done:;  // label reached via goto inside the ASCENDC branch
#endif

        // FFN gate/up/down: W8 > NZ > ND. Phase A.1: when inplace is active,
        // route the down-proj output into normed_dev_ so the subsequent
        // InplaceAddRmsNorm's x1Ref already holds the FFN addend.
        // Phase B: when FFN_V3 is active we collapse the 5-op chain into
        // aclnnFFNV3; the capturable variant mirrors eager so aclGraph
        // re-capture (when enabled post-B) picks up the fused op.
        void *post_ffn_rhs_dev = cp_inplace_ars_applied_ ? normed_dev_
                                                         : ffn_out_dev_;
        if (cp_ffn_v3_applied_) {
            aclTensor *t_n_row   = tensor_2d(normed_dev_, 1, cp_hidden_, ACL_FLOAT16);
            aclTensor *t_ffn_row = tensor_2d(post_ffn_rhs_dev, 1, cp_hidden_,
                                              ACL_FLOAT16);
            ffn_v3_swiglu_(t_n_row,
                           layer_w_[il].gate_up_w_i8,
                           layer_w_[il].gate_up_scale,
                           layer_w_[il].down_proj_w_i8,
                           layer_w_[il].down_proj_scale,
                           t_ffn_row);
            g_cann.aclDestroyTensor(t_n_row);
            g_cann.aclDestroyTensor(t_ffn_row);
        } else if (w8_applied_) {
            aclTensor *t_n_row    = tensor_2d(normed_dev_, 1, cp_hidden_, ACL_FLOAT16);
            aclTensor *t_gate_row = tensor_2d(gate_dev_, 1, inter_, ACL_FLOAT16);
            aclTensor *t_up_row   = tensor_2d(up_dev_,   1, inter_, ACL_FLOAT16);
            aclTensor *t_ffn_row  = tensor_2d(post_ffn_rhs_dev, 1, cp_hidden_,
                                               ACL_FLOAT16);
            w8_matmul_(t_n_row, layer_w_[il].gate_proj_w_i8,
                        layer_w_[il].gate_proj_scale, inter_, cp_hidden_,
                        t_gate_row);
            w8_matmul_(t_n_row, layer_w_[il].up_proj_w_i8,
                        layer_w_[il].up_proj_scale,   inter_, cp_hidden_,
                        t_up_row);
            CANN_OP(Silu, t_.gate_flat, t_.gate_flat);
            CANN_OP(InplaceMul, t_.gate_flat, t_.up_flat);
            w8_matmul_(t_gate_row, layer_w_[il].down_proj_w_i8,
                        layer_w_[il].down_proj_scale, cp_hidden_, inter_,
                        t_ffn_row);
            g_cann.aclDestroyTensor(t_n_row);
            g_cann.aclDestroyTensor(t_gate_row);
            g_cann.aclDestroyTensor(t_up_row);
            g_cann.aclDestroyTensor(t_ffn_row);
        } else if (nz_applied_) {
            auto wT_nz = [&](void *buf, int64_t rows, int64_t cols) {
                int64_t shape[2]   = {cols, rows};
                int64_t strides[2] = {1, cols};
                return make_tensor(buf, 2, shape, strides, ACL_FLOAT16,
                                    ACL_FORMAT_FRACTAL_NZ);
            };
            aclTensor *t_wg_T = wT_nz(layer_w_[il].gate_proj_w, inter_,      cp_hidden_);
            aclTensor *t_wu_T = wT_nz(layer_w_[il].up_proj_w,   inter_,      cp_hidden_);
            aclTensor *t_wd_T = wT_nz(layer_w_[il].down_proj_w, cp_hidden_,  inter_);
            aclTensor *t_n_row    = tensor_2d(normed_dev_, 1, cp_hidden_);
            aclTensor *t_gate_row = tensor_2d(gate_dev_, 1, inter_);
            aclTensor *t_up_row   = tensor_2d(up_dev_,   1, inter_);
            aclTensor *t_ffn_row  = tensor_2d(post_ffn_rhs_dev, 1, cp_hidden_);
            CANN_OP(Mm, t_n_row, t_wg_T, t_gate_row, (int8_t)0);
            CANN_OP(Mm, t_n_row, t_wu_T, t_up_row,   (int8_t)0);
            CANN_OP(Silu, t_.gate_flat, t_.gate_flat);
            CANN_OP(InplaceMul, t_.gate_flat, t_.up_flat);
            CANN_OP(Mm, t_gate_row, t_wd_T, t_ffn_row, (int8_t)0);
            g_cann.aclDestroyTensor(t_wg_T);
            g_cann.aclDestroyTensor(t_wu_T);
            g_cann.aclDestroyTensor(t_wd_T);
            g_cann.aclDestroyTensor(t_n_row);
            g_cann.aclDestroyTensor(t_gate_row);
            g_cann.aclDestroyTensor(t_up_row);
            g_cann.aclDestroyTensor(t_ffn_row);
        } else {
            if (cp_inplace_ars_applied_) {
                aclTensor *t_ffn_col = tensor_2d(normed_dev_, cp_hidden_, 1,
                                                  ACL_FLOAT16);
                CANN_OP(Mm, lt.gate_proj, t_.normed_col, t_.gate_col,
                        /*cubeMathType=*/0);
                CANN_OP(Mm, lt.up_proj,   t_.normed_col, t_.up_col,
                        /*cubeMathType=*/0);
                CANN_OP(Silu, t_.gate_flat, t_.gate_flat);
                CANN_OP(InplaceMul, t_.gate_flat, t_.up_flat);
                CANN_OP(Mm, lt.down_proj, t_.gate_col, t_ffn_col,
                        /*cubeMathType=*/0);
                g_cann.aclDestroyTensor(t_ffn_col);
            } else {
                CANN_OP(Mm, lt.gate_proj, t_.normed_col, t_.gate_col,
                        /*cubeMathType=*/0);
                CANN_OP(Mm, lt.up_proj,   t_.normed_col, t_.up_col,
                        /*cubeMathType=*/0);
                CANN_OP(Silu, t_.gate_flat, t_.gate_flat);
                CANN_OP(InplaceMul, t_.gate_flat, t_.up_flat);
                CANN_OP(Mm, lt.down_proj, t_.gate_col, t_.ffn_out_col,
                        /*cubeMathType=*/0);
            }
        }

        const bool is_last_eager = (il + 1 == layers_to_run);
        const aclTensor *next_gamma_eager =
            is_last_eager ? t_.final_norm_f16 : t_.layer[il + 1].input_ln_f16;
        if (cp_fusion_applied_ && cp_inplace_ars_applied_) {
            // Phase A.1 post-FFN inplace: x1Ref=normed_dev_ (holds down-proj
            // output), x2Ref=residual_dev_. After the call: normed_dev_
            // holds rmsnorm(ffn+residual, next_gamma), residual_dev_ holds
            // the new running residual. On the last layer the next_gamma is
            // final_norm, so normed_dev_ becomes the final normalised
            // hidden — exactly what t_.output_f16 views for the F32 cast.
            aclTensor *t_x1 =
                tensor_2d(normed_dev_,   1, cp_hidden_, ACL_FLOAT16);
            aclTensor *t_x2 =
                tensor_2d(residual_dev_, 1, cp_hidden_, ACL_FLOAT16);
            uint64_t ws_needed = 0; aclOpExecutor *exec = nullptr;
            ACL_CHECK_RET(g_cann.aclnnInplaceAddRmsNormGetWorkspaceSize(
                t_x1, t_x2, next_gamma_eager, (double)eps_,
                t_.rstd_11,
                &ws_needed, &exec));
            ensure_workspace((size_t)ws_needed);
            void *ws = ws_needed > 0 ? workspace_dev_ : nullptr;
            ACL_CHECK_RET(g_cann.aclnnInplaceAddRmsNorm(ws, ws_needed, exec,
                                                          stream_));
            g_cann.aclDestroyTensor(t_x1);
            g_cann.aclDestroyTensor(t_x2);
        } else if (cp_fusion_applied_) {
            // W3b: fuse `cur = residual + ffn_out` with the NEXT step's
            // RmsNorm. For il < last layer: gamma = input_ln[il+1], priming
            // the next layer so its entry can skip both the `residual=cur`
            // d2d AND the `RmsNorm(cur, input_ln)`. For il == last layer:
            // gamma = final_norm, subsuming the post-loop RmsNorm.
            aclTensor *t_resid_row =
                tensor_2d(residual_dev_, 1, cp_hidden_, ACL_FLOAT16);
            aclTensor *t_ffn_row =
                tensor_2d(ffn_out_dev_,  1, cp_hidden_, ACL_FLOAT16);
            uint64_t ws_needed = 0;
            aclOpExecutor *exec = nullptr;
            ACL_CHECK_RET(g_cann.aclnnAddRmsNormGetWorkspaceSize(
                t_resid_row, t_ffn_row, next_gamma_eager, (double)eps_,
                t_.normed_row, t_.rstd_11, t_.cur_row,
                &ws_needed, &exec));
            ensure_workspace((size_t)ws_needed);
            void *ws = ws_needed > 0 ? workspace_dev_ : nullptr;
            ACL_CHECK_RET(g_cann.aclnnAddRmsNorm(ws, ws_needed, exec,
                                                   stream_));
            g_cann.aclDestroyTensor(t_resid_row);
            g_cann.aclDestroyTensor(t_ffn_row);
        } else {
            // cur = residual + ffn_out  (F16 add)
            CANN_OP(Add, t_.residual_flat, t_.ffn_out_flat, one_scalar_,
                    t_.cur_flat);
        }
    }

    // Final RmsNorm: only needed when fusion is OFF. With fusion, the LAST
    // layer's post-FFN AddRmsNorm already wrote `rmsnorm(cur, final_norm)`
    // into normed_dev_ (which is what t_.output_f16 views). Under inplace
    // fusion, the final InplaceAddRmsNorm above used final_norm for the
    // last layer so normed_dev_ holds the same result.
    if (!cp_fusion_applied_) {
        CANN_OP(RmsNorm, t_.cur_row, t_.final_norm, (double)eps_,
                t_.normed_row, t_.rstd_11);
    }

    // Cast F16 hidden -> F32 staging (queued async; D2H + sync moved to _fetch).
    CANN_OP(Cast, t_.output_f16, ACL_FLOAT, t_.output_f32);

    // Record event so `forward_one_token_fetch` (or another stream via
    // aclrtStreamWaitEvent) can fence against forward completion. M3'new'
    // lets the caller skip this when chaining a second forward in the
    // same submission (forward_two_tokens_launch's pos-0 leg).
    if (record_event && forward_done_event_ && g_cann.aclrtRecordEvent) {
        ACL_CHECK_RET(g_cann.aclrtRecordEvent(forward_done_event_, stream_));
    }

    g_cann.aclDestroyTensor(t_cos);
    g_cann.aclDestroyTensor(t_sin);
}

// M3'new': batched prefill of CP positions 0+1 on `stream_` with no host
// sync between the two forwards. See header for semantics.
void CpCannEngine::forward_two_tokens_launch(const float *input0,
                                              const float *input1,
                                              aclrtEvent wait_event) {
    assert(ready_);
    // Pos 0: upload via async memcpy so pos 1's upload further down is
    // stream-ordered against it without the host-synchronous memcpy gap.
    // Skip the tail event record — it's folded into the pos-1 call below.
    forward_one_token_launch(input0, /*pos=*/0, wait_event,
                              /*use_async_memcpy=*/true,
                              /*record_event=*/false);
    // Pos 1: stream-ordered async upload again. The pos-0 input projection
    // Mm on `stream_` has already consumed `input_stage_f32_dev_` by the
    // time this memcpy runs on-device, so overwriting the buffer is safe.
    // Record the done event at the tail — callers fence on this just like
    // they would after the second sequential `forward_one_token_launch`.
    forward_one_token_launch(input1, /*pos=*/1, /*wait_event=*/nullptr,
                              /*use_async_memcpy=*/true,
                              /*record_event=*/true);
}

void CpCannEngine::forward_one_token_fetch(float *hidden_out) {
    assert(ready_);
    if (forward_done_event_ && g_cann.aclrtSynchronizeEvent) {
        ACL_CHECK_RET(g_cann.aclrtSynchronizeEvent(forward_done_event_));
    } else {
        ACL_CHECK_RET(g_cann.aclrtSynchronizeStream(stream_));
    }
    ACL_CHECK_RET(g_cann.aclrtMemcpy(
        hidden_out, cp_hidden_ * sizeof(float),
        output_stage_f32_dev_, cp_hidden_ * sizeof(float),
        ACL_MEMCPY_DEVICE_TO_HOST));
}

void CpCannEngine::forward_one_token(const float *input_talker_space,
                                      int pos, float *hidden_out) {
    forward_one_token_launch(input_talker_space, pos, nullptr);
    forward_one_token_fetch(hidden_out);
}

// W1 helper: sync without D2H. After this returns, output_stage_f32_dev_
// holds the hidden state ready for the next on-device consumer.
void CpCannEngine::forward_one_token_sync() {
    assert(ready_);
    if (forward_done_event_ && g_cann.aclrtSynchronizeEvent) {
        ACL_CHECK_RET(g_cann.aclrtSynchronizeEvent(forward_done_event_));
    } else {
        ACL_CHECK_RET(g_cann.aclrtSynchronizeStream(stream_));
    }
    // WSPOOL: after the sync above, no aclnn op is still reading the
    // workspace — safe to free any buffers retained by growths in this
    // forward pass.
    reset_workspace_retained_();
}

// ============================================================================
// W1: NPU lm_head port
// ============================================================================

bool CpCannEngine::init_lm_head_(const std::vector<std::vector<float>> &heads,
                                  int vocab_size) {
    if (lm_head_ready_) return true;
    if (heads.empty() || vocab_size <= 0) {
        fprintf(stderr, "[cp_cann] init_lm_head_: empty heads or zero "
                "vocab (%zu, %d)\n", heads.size(), vocab_size);
        return false;
    }
    const size_t E = sizeof(uint16_t);
    const size_t per = (size_t)vocab_size * cp_hidden_;
    lm_head_w_dev_.assign(heads.size(), nullptr);
    for (size_t g = 0; g < heads.size(); ++g) {
        if (heads[g].size() != per) {
            fprintf(stderr, "[cp_cann] lm_head[%zu] size %zu != expected %zu "
                    "(vocab=%d, cp_hidden=%d)\n",
                    g, heads[g].size(), per, vocab_size, cp_hidden_);
            return false;
        }
        alloc_dev(&lm_head_w_dev_[g], per * E);
        upload_f16(lm_head_w_dev_[g], heads[g].data(), per);
    }
    alloc_dev(&logits_f16_dev_,       vocab_size * E);
    alloc_dev(&logits_stage_f32_dev_, vocab_size * sizeof(float));
    lm_head_vocab_size_ = vocab_size;
    lm_head_n_groups_   = (int)heads.size();
    lm_head_ready_      = true;
    printf("[cp_cann] W1: uploaded %d lm_head weights (F16, vocab=%d, "
           "cp_hidden=%d) — NPU lm_head path ENABLED\n",
           lm_head_n_groups_, lm_head_vocab_size_, cp_hidden_);
    return true;
}

void CpCannEngine::forward_lm_head(int group_idx) {
    assert(ready_);
    assert(lm_head_ready_);
    assert(group_idx >= 0 && group_idx < lm_head_n_groups_);

    // Fence against the forward_one_token that produced output_stage_f32_dev_
    // on `stream_`. Same stream ⇒ ops are serial anyway, so this is only a
    // safety net in case a future caller splits across streams.
    // (No explicit wait needed for same-stream ordering.)

    // 1. Cast the F32 hidden (output_stage_f32_dev_ [cp_hidden]) to F16.
    //    We reuse `cur_dev_` (F16 [cp_hidden]) as staging — it's only touched
    //    at layer 0 of the next forward and we run lm_head between forwards.
    {
        aclTensor *t_h_f32 = tensor_1d(output_stage_f32_dev_, cp_hidden_,
                                        ACL_FLOAT);
        aclTensor *t_h_f16 = tensor_1d(cur_dev_, cp_hidden_, ACL_FLOAT16);
        CANN_OP(Cast, t_h_f32, ACL_FLOAT16, t_h_f16);
        g_cann.aclDestroyTensor(t_h_f32);
        g_cann.aclDestroyTensor(t_h_f16);
    }

    // 2. Matmul: logits[vocab] = W[vocab, cp_hidden] @ h[cp_hidden]
    //    Existing CP convention (see q/k/v/o projections): aclnnMm takes
    //    a 2-D weight [out, in] and a 2-D activation [in, 1] and writes
    //    [out, 1]. Mirror that here so the NZ/ND kernel path is identical.
    {
        aclTensor *t_w   = tensor_2d(lm_head_w_dev_[group_idx],
                                      lm_head_vocab_size_, cp_hidden_,
                                      ACL_FLOAT16);
        aclTensor *t_x   = tensor_2d(cur_dev_, cp_hidden_, 1, ACL_FLOAT16);
        aclTensor *t_y   = tensor_2d(logits_f16_dev_,
                                      lm_head_vocab_size_, 1, ACL_FLOAT16);
        CANN_OP(Mm, t_w, t_x, t_y, /*cubeMathType=*/0);
        g_cann.aclDestroyTensor(t_w);
        g_cann.aclDestroyTensor(t_x);
        g_cann.aclDestroyTensor(t_y);
    }

    // 3. Cast F16 logits -> F32 staging (async). D2H + sync happen in fetch.
    {
        aclTensor *t_l_f16 = tensor_1d(logits_f16_dev_,      lm_head_vocab_size_,
                                        ACL_FLOAT16);
        aclTensor *t_l_f32 = tensor_1d(logits_stage_f32_dev_, lm_head_vocab_size_,
                                        ACL_FLOAT);
        CANN_OP(Cast, t_l_f16, ACL_FLOAT, t_l_f32);
        g_cann.aclDestroyTensor(t_l_f16);
        g_cann.aclDestroyTensor(t_l_f32);
    }
}

void CpCannEngine::fetch_logits(float *host_out) {
    assert(ready_);
    assert(lm_head_ready_);
    ACL_CHECK_RET(g_cann.aclrtSynchronizeStream(stream_));
    ACL_CHECK_RET(g_cann.aclrtMemcpy(
        host_out, lm_head_vocab_size_ * sizeof(float),
        logits_stage_f32_dev_, lm_head_vocab_size_ * sizeof(float),
        ACL_MEMCPY_DEVICE_TO_HOST));
}
